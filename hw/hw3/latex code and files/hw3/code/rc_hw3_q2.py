import cv2

class GradCAM:
    def __init__(self, model, target_layer):
        self.model = model
        self.target_layer = target_layer
        self.gradients = None
        self.activations = None

        self.target_layer.register_forward_hook(self.save_activation)
        self.target_layer.register_full_backward_hook(self.save_gradient)

    def save_activation(self, module, input, output):
        self.activations = output.detach()

    def save_gradient(self, module, grad_input, grad_output):
        self.gradients = grad_output[0].detach()

    def generate_heatmap(self, input_image, class_idx):
        self.model.zero_grad()
        output = self.model(input_image)
        target = output[0, class_idx]
        target.backward()

        weights = torch.mean(self.gradients, dim=[2, 3])[0]
        heatmap = torch.zeros(self.activations.shape[2:], dtype=torch.float32, device=input_image.device)
        for i, w in enumerate(weights):
            heatmap += w * self.activations[0, i, :, :]

        heatmap = torch.clamp(heatmap, min=0)
        heatmap /= torch.max(heatmap) if torch.max(heatmap) != 0 else 1
        return heatmap.cpu().numpy()


