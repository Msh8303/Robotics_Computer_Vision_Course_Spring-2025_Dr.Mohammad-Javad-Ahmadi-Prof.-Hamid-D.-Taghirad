augmenter = A.Compose([
    A.HorizontalFlip(p=0.5),
    A.VerticalFlip(p=0.2),
    A.Rotate(limit=30, p=0.7, border_mode=cv2.BORDER_CONSTANT),
    A.RandomBrightnessContrast(brightness_limit=0.3, contrast_limit=0.3, p=0.7),
    A.HueSaturationValue(hue_shift_limit=10, sat_shift_limit=20, val_shift_limit=10, p=0.5),
    A.Affine(scale=(0.8, 1.2), translate_percent=(0.1, 0.1), rotate=(-15, 15), p=0.5),
    A.Resize(300, 300)
], p=1.0)

def augment_and_remove_majors(input_dir, output_dir, csv_path, output_csv_path):
    if not os.path.exists(csv_path):
        print(f"❌ Error: CSV not found at {csv_path}")
        return


    df = pd.read_csv(csv_path)
    df.columns = df.columns.str.strip()


    cols_to_remove = ['Cornea', 'Pupil']
    df = df.drop(columns=[col for col in cols_to_remove if col in df.columns])


    tool_classes = [c for c in df.columns if c != 'filename']


    class_counts = df[tool_classes].sum()
    target_count = int(class_counts.max())
    print(f"🎯 New Target Count per class: {target_count} (based on {class_counts.idxmax()})")

    os.makedirs(output_dir, exist_ok=True)
    final_rows = []


    print("Step 1: Copying original images with reduced labels...")
    for idx, row in tqdm(df.iterrows(), total=len(df), desc="Originals"):
        src_path = os.path.join(input_dir, row["filename"])
        if os.path.exists(src_path):
            img = cv2.imread(src_path)
            if img is not None:

                if row[tool_classes].sum() > 0:
                    cv2.imwrite(os.path.join(output_dir, row["filename"]), img)
                    final_rows.append(row.copy())


    print("Step 2: Balancing tools via augmentation...")
    current_df = pd.DataFrame(final_rows)
    current_counts = current_df[tool_classes].sum()

    for cls in tool_classes:
        needed = target_count - int(current_counts[cls])
        if needed <= 0: continue

        print(f"➕ Augmenting {cls}: Need {needed}")
        class_samples = current_df[current_df[cls] == 1]
        if len(class_samples) == 0: continue

        count_added = 0
        while count_added < needed:
            sample = class_samples.sample(n=1).iloc[0]
            src_path = os.path.join(output_dir, sample["filename"])
            image = cv2.imread(src_path)
            if image is None: continue

            augmented = augmenter(image=image)["image"]
            new_filename = f"aug_{cls.replace(' ', '_')}_{count_added}_{sample['filename']}"
            cv2.imwrite(os.path.join(output_dir, new_filename), augmented)

            new_row = sample.copy()
            new_row["filename"] = new_filename
            final_rows.append(new_row)
            count_added += 1


    df_final = pd.DataFrame(final_rows).sample(frac=1).reset_index(drop=True)
    df_final.to_csv(output_csv_path, index=False)

    print(f"\n✅ Done! Dataset is now balanced without Cornea/Pupil.")
    print(f"📊 Total images: {len(df_final)}")
    print(f"📁 Images saved to: {output_dir}")
    print(f"📄 CSV saved to: {output_csv_path}")


