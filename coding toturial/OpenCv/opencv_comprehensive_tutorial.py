"""---
# 16. 🎬 Video Basics

OpenCV reads video frame-by-frame with `cv2.VideoCapture`. Live webcam access (`VideoCapture(0)`) **doesn't work in Colab** — Colab runs on a remote server with no camera. But reading a video **file** works fine.

Below we build a synthetic video, then read it back and inspect the metadata.

[colab_webcam.ipynb](https://colab.research.google.com/drive/1QnC7lV7oVFk5OZCm75fqbLAfD9qBy9bw#scrollTo=L6pCmkJrUC9g)
"""

# Build a tiny synthetic video: a moving white circle on black
video_path = 'images/demo.mp4'
fps = 20
size = (320, 240)  # (width, height)

# 'mp4v' is a widely supported codec in Colab's OpenCV build
fourcc = cv2.VideoWriter_fourcc(*'mp4v')
writer = cv2.VideoWriter(video_path, fourcc, fps, size)
print(f"VideoWriter opened: {writer.isOpened()}")

n_frames = 60
for i in range(n_frames):
    frame = np.zeros((size[1], size[0], 3), dtype=np.uint8)
    x_pos = int(20 + (size[0] - 40) * (i / (n_frames - 1)))
    cv2.circle(frame, (x_pos, size[1] // 2), 20, (0, 255, 255), -1)
    cv2.putText(frame, f"Frame {i+1}/{n_frames}", (10, 25),
                cv2.FONT_HERSHEY_SIMPLEX, 0.6, (255, 255, 255), 1)
    writer.write(frame)

writer.release()
print(f"Wrote {n_frames} frames -> {video_path} ({os.path.getsize(video_path):,} bytes)")

# Now read the video back and report its properties
cap = cv2.VideoCapture(video_path)
print(f"VideoCapture opened: {cap.isOpened()}")

n_frames_read = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
fps_read      = cap.get(cv2.CAP_PROP_FPS)
w_read        = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
h_read        = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
print(f"Frame count: {n_frames_read}")
print(f"FPS        : {fps_read}")
print(f"Resolution : {w_read}x{h_read}")
print(f"Duration   : {n_frames_read / fps_read:.2f} s")

# Iterate through the frames
frame_idx = 0
sample_frames = []  # we'll keep a few to display
while True:
    ret, frame = cap.read()
    if not ret:
        break
    if frame_idx in (0, n_frames_read // 2, n_frames_read - 1):
        sample_frames.append((frame_idx, frame))
    frame_idx += 1

cap.release()
print(f"\nFinished reading. Total frames consumed: {frame_idx}")
print(f"Sampled frames at indices: {[i for i, _ in sample_frames]}")

show_grid(
    [f for _, f in sample_frames],
    [f"Frame {i}" for i, _ in sample_frames],
    cols=3, figsize=(13, 4)
)

