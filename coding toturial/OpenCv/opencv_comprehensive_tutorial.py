"""---
# 8. 🎯 Thresholding

Thresholding turns a grayscale image into a **binary** image — every pixel becomes either 0 or 255 (or `maxval`) based on whether it's above or below a threshold value. It's the simplest way to do segmentation.

Three flavors:
1. **Simple** — one global threshold, you pick the value
2. **Otsu** — automatically picks the threshold by analyzing the histogram
3. **Adaptive** — threshold computed locally per region (great for uneven lighting)

"""

