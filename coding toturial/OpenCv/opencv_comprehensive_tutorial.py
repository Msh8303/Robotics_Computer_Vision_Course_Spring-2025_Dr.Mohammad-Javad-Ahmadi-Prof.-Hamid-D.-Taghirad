"""---
# 10. 🔬 Morphological Operations

These operations are applied to **binary images** and use a **structuring element** (kernel) to grow or shrink shapes:

| Operation | Effect |
|-----------|--------|
| **Erosion** | Shrinks white regions — removes small specks |
| **Dilation** | Grows white regions — fills small holes |
| **Opening** | Erosion then dilation — removes noise but preserves shape size |
| **Closing** | Dilation then erosion — closes small holes |
| **Gradient** | Dilation − erosion — gives the outline of objects |

"""

# Build a synthetic binary image with a shape and some noise
binary = np.zeros((300, 300), dtype=np.uint8)
cv2.rectangle(binary, (75, 75), (225, 225), 255, -1)
cv2.circle(binary, (150, 150), 30, 0, -1)  # carve a hole inside

# Add salt noise (white specks outside) and pepper noise (black specks inside)
np.random.seed(0)
noise_pts = np.random.randint(0, 300, (200, 2))
for y, x in noise_pts[:100]:
    binary[y, x] = 255
for y, x in noise_pts[100:]:
    if binary[y, x] == 255:
        binary[y, x] = 0

# Structuring element — a 5x5 square
kernel = np.ones((5, 5), np.uint8)
print(f"Kernel shape: {kernel.shape}")
print(kernel)

erosion  = cv2.erode(binary,  kernel, iterations=1)
dilation = cv2.dilate(binary, kernel, iterations=1)
opening  = cv2.morphologyEx(binary, cv2.MORPH_OPEN,    kernel)
closing  = cv2.morphologyEx(binary, cv2.MORPH_CLOSE,   kernel)
gradient = cv2.morphologyEx(binary, cv2.MORPH_GRADIENT, kernel)

show_grid(
    [binary, erosion, dilation, opening, closing, gradient],
    ["Original (noisy)", "Erosion", "Dilation",
     "Opening (denoise)", "Closing (fill holes)", "Gradient (outline)"],
    cols=3, figsize=(13, 8)
)

"""---
# 11. ⚡ Edge Detection

Edges are places where pixel intensity changes sharply. We'll cover three classic edge detectors:

| Detector | What it does |
|----------|--------------|
| **Sobel** | Computes the gradient in x or y direction (1st derivative) |
| **Laplacian** | Combines x and y second derivatives — single output |
| **Canny** | The gold standard — multi-stage: Gaussian blur → gradients → non-max suppression → hysteresis thresholding |

"""

# Slight blur first — edge detectors are sensitive to noise
blurred = cv2.GaussianBlur(gray, (5, 5), 0)

# Sobel: ddepth=cv2.CV_64F to capture negative gradients, then convert back to uint8
sobel_x = cv2.Sobel(blurred, cv2.CV_64F, dx=1, dy=0, ksize=3)
sobel_y = cv2.Sobel(blurred, cv2.CV_64F, dx=0, dy=1, ksize=3)
sobel_x_abs = cv2.convertScaleAbs(sobel_x)
sobel_y_abs = cv2.convertScaleAbs(sobel_y)
sobel_combined = cv2.addWeighted(sobel_x_abs, 0.5, sobel_y_abs, 0.5, 0)

# Laplacian
laplacian = cv2.Laplacian(blurred, cv2.CV_64F, ksize=3)
laplacian_abs = cv2.convertScaleAbs(laplacian)

# Canny — pass two thresholds (low, high). Lower=more edges, Higher=stricter.
canny_loose  = cv2.Canny(blurred, 50, 150)
canny_strict = cv2.Canny(blurred, 100, 200)

print(f"Sobel-X dtype: {sobel_x.dtype} (CV_64F to keep sign), "
      f"range: [{sobel_x.min():.1f}, {sobel_x.max():.1f}]")
print(f"After convertScaleAbs: dtype={sobel_x_abs.dtype}, "
      f"range=[{sobel_x_abs.min()}, {sobel_x_abs.max()}]")
print(f"Canny output is binary: unique values = {np.unique(canny_loose)}")
print(f"Edge pixel count — loose Canny: {(canny_loose > 0).sum():,}, "
      f"strict Canny: {(canny_strict > 0).sum():,}")

show_grid(
    [gray, sobel_x_abs, sobel_y_abs, sobel_combined, laplacian_abs, canny_loose, canny_strict],
    ["Gray", "Sobel-X", "Sobel-Y", "Sobel combined",
     "Laplacian", "Canny (50,150)", "Canny (100,200)"],
    cols=3, figsize=(13, 11)
)

"""---
# 12. 🔵 Contours

A **contour** is a curve joining all continuous points along a boundary that share the same color/intensity. They're the foundation of shape analysis.

Workflow:
1. Convert to grayscale
2. Threshold (or run edge detection) to get a binary image
3. Call `cv2.findContours(...)`
4. Draw or analyze the contours

`findContours` returns:
- `contours`: a Python list, one element per contour, each a NumPy array of `(x, y)` points
- `hierarchy`: parent/child relationships between contours

"""

# Make a synthetic image with several distinct shapes — clearer than a photo
shapes = np.full((400, 600, 3), 255, dtype=np.uint8)  # white background
cv2.rectangle(shapes, ( 50,  50), (170, 170), (  0,   0,   0), -1)
cv2.circle(shapes,    (300, 110), 60,         (  0,   0,   0), -1)
pts_tri = np.array([[450, 50], [550, 50], [500, 170]], np.int32)
cv2.fillPoly(shapes, [pts_tri], (0, 0, 0))
cv2.rectangle(shapes, ( 50, 230), (170, 350), (  0,   0,   0), -1)
cv2.ellipse(shapes,   (350, 290), (90, 50), 0, 0, 360, (0, 0, 0), -1)

# Convert to grayscale, then threshold (invert so shapes are white = foreground)
shapes_gray = cv2.cvtColor(shapes, cv2.COLOR_BGR2GRAY)
_, shapes_bw = cv2.threshold(shapes_gray, 127, 255, cv2.THRESH_BINARY_INV)

# Find contours
# RETR_EXTERNAL = only the outermost contours
# CHAIN_APPROX_SIMPLE = compress horiz/vert/diag segments to endpoints (saves memory)
contours, hierarchy = cv2.findContours(shapes_bw, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

print(f"Number of contours found: {len(contours)}")
print(f"Hierarchy shape         : {hierarchy.shape if hierarchy is not None else None}")
print(f"\n{'#':<3} {'Points':<8} {'Area':<10} {'Perimeter':<12} {'BBox (x,y,w,h)'}")
print("-" * 60)

for i, c in enumerate(contours):
    area = cv2.contourArea(c)
    peri = cv2.arcLength(c, closed=True)
    x, y, bw_box, bh_box = cv2.boundingRect(c)
    print(f"{i:<3} {len(c):<8} {area:<10.1f} {peri:<12.1f} ({x},{y},{bw_box},{bh_box})")

# Draw all contours, plus the bounding box of each
shapes_with_contours = shapes.copy()
cv2.drawContours(shapes_with_contours, contours, -1, (0, 255, 0), 2)
for c in contours:
    x, y, bw_box, bh_box = cv2.boundingRect(c)
    cv2.rectangle(shapes_with_contours, (x, y), (x + bw_box, y + bh_box), (0, 0, 255), 2)

show_grid(
    [shapes, shapes_bw, shapes_with_contours],
    ["Synthetic shapes", "Binary (inverted)", "Contours (green) + boxes (red)"],
    cols=3, figsize=(14, 5)
)

