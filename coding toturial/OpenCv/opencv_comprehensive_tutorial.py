"""---
# 14. 📍 Feature Detection

A **feature** is a distinctive image location — typically a corner — that can be re-detected reliably even if the image is rotated, scaled, or slightly distorted. We'll demo:

- **Harris corners** — classic corner detector
- **Shi-Tomasi (`goodFeaturesToTrack`)** — improved Harris, returns top N corners
- **ORB** — fast, free, modern keypoint detector + descriptor (drop-in replacement for SIFT/SURF)

"""

# Harris corner detector
gray_f32 = np.float32(gray)
harris = cv2.cornerHarris(gray_f32, blockSize=2, ksize=3, k=0.04)
# Dilate to make corner markers visible
harris_dilated = cv2.dilate(harris, None)
img_harris = img.copy()
img_harris[harris_dilated > 0.01 * harris_dilated.max()] = [0, 0, 255]  # mark in red
print(f"Harris response: shape={harris.shape}, dtype={harris.dtype}, "
      f"range=[{harris.min():.4f}, {harris.max():.4f}]")
print(f"Harris corners marked: {(harris_dilated > 0.01 * harris_dilated.max()).sum():,} pixels")

# Shi-Tomasi
corners_st = cv2.goodFeaturesToTrack(gray, maxCorners=50, qualityLevel=0.01, minDistance=10)
img_st = img.copy()
print(f"\nShi-Tomasi corners: {len(corners_st)} points")
for c in corners_st:
    x, y = c.ravel().astype(int)
    cv2.circle(img_st, (x, y), 5, (0, 255, 0), 2)

# ORB
orb = cv2.ORB_create(nfeatures=200)
keypoints, descriptors = orb.detectAndCompute(gray, None)
img_orb = cv2.drawKeypoints(img, keypoints, None, color=(0, 255, 0),
                             flags=cv2.DRAW_MATCHES_FLAGS_DRAW_RICH_KEYPOINTS)
print(f"\nORB keypoints     : {len(keypoints)}")
if descriptors is not None:
    print(f"ORB descriptors    : shape={descriptors.shape}, dtype={descriptors.dtype} "
          f"({descriptors.shape[1]} bytes per descriptor)")

show_grid(
    [img_harris, img_st, img_orb],
    ["Harris corners (red)", "Shi-Tomasi top-50 (green circles)", "ORB keypoints"],
    cols=3, figsize=(14, 5)
)

