"""## 1.3 Helper function to display images

Because OpenCV stores color images in **BGR** order, but matplotlib expects **RGB**, we'll write a small helper that handles both color and grayscale images correctly. We'll use this helper throughout the notebook so every visualization is consistent.

"""

def show(image, title="Image", cmap=None, figsize=(8, 6)):
    """
    Display an OpenCV image inline using matplotlib.

    Parameters
    ----------
    image  : np.ndarray  -- the image (grayscale 2D, or BGR 3D)
    title  : str         -- plot title
    cmap   : str or None -- matplotlib colormap (auto-picks 'gray' for 2D)
    figsize: tuple       -- figure size in inches
    """
    plt.figure(figsize=figsize)

    if len(image.shape) == 2:
        # Grayscale image: use a gray colormap
        plt.imshow(image, cmap=cmap or 'gray', vmin=0, vmax=255)
    else:
        # Color image: convert BGR (OpenCV default) -> RGB (matplotlib default)
        plt.imshow(cv2.cvtColor(image, cv2.COLOR_BGR2RGB))

    plt.title(title)
    plt.axis('off')
    plt.show()


def show_grid(images, titles, cols=2, figsize=(12, 8)):
    """
    Display multiple images side by side in a grid.
    Useful for before/after comparisons.
    """
    n = len(images)
    rows = (n + cols - 1) // cols
    plt.figure(figsize=figsize)
    for i, (img, t) in enumerate(zip(images, titles)):
        plt.subplot(rows, cols, i + 1)
        if len(img.shape) == 2:
            plt.imshow(img, cmap='gray', vmin=0, vmax=255)
        else:
            plt.imshow(cv2.cvtColor(img, cv2.COLOR_BGR2RGB))
        plt.title(t)
        plt.axis('off')
    plt.tight_layout()
    plt.show()


