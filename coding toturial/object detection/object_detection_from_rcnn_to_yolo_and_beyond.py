"""### 15.2 The multi-task loss

For each image we (a) match anchors to GT (Section 11), then compute:

- **Classification loss** — cross-entropy over `NUM_CLASSES` for *positive* and *negative* anchors (ignored anchors excluded). Because negatives vastly outnumber positives, we apply **hard-negative mining** (SSD's trick): keep only the highest-loss negatives at a 3:1 ratio to positives. (Section 17 replaces this with Focal Loss.)
- **Regression loss** — **Smooth L1** (Huber) between predicted and target deltas, computed for *positive anchors only* (background has no box to regress).

Smooth L1 is L2 for small errors (smooth gradient near 0) and L1 for large errors (robust to outliers) — the standard box-regression loss since Fast R-CNN.
"""

import torch.nn.functional as F

# Helper functions duplicated here to ensure this cell is self-contained and runnable.
# Original definitions are in other cells: cca97a46, da27a2b4, 33cadda6, 67eb0bd8.

def xyxy_to_cxcywh(boxes: torch.Tensor) -> torch.Tensor:
    """(x_min, y_min, x_max, y_max) -> (cx, cy, w, h)."""
    x1, y1, x2, y2 = boxes.unbind(-1)
    return torch.stack([(x1 + x2) / 2, (y1 + y2) / 2, x2 - x1, y2 - y1], dim=-1)

def encode_boxes(gt_boxes, anchors):
    """xyxy gt & anchors -> (tx,ty,tw,th) deltas. Both (N,4)."""
    g = xyxy_to_cxcywh(gt_boxes); a = xyxy_to_cxcywh(anchors)
    tx = (g[:, 0] - a[:, 0]) / a[:, 2]
    ty = (g[:, 1] - a[:, 1]) / a[:, 3]
    tw = torch.log(g[:, 2] / a[:, 2])
    th = torch.log(g[:, 3] / a[:, 3])
    return torch.stack([tx, ty, tw, th], dim=1)

def box_area(boxes: torch.Tensor) -> torch.Tensor:
    """Area of xyxy boxes. boxes: (..., 4)."""
    return (boxes[..., 2] - boxes[..., 0]).clamp(min=0) * \
           (boxes[..., 3] - boxes[..., 1]).clamp(min=0)

def box_iou(boxes1: torch.Tensor, boxes2: torch.Tensor) -> torch.Tensor:
    """Pairwise IoU. boxes1:(N,4) boxes2:(M,4) -> (N,M). Both xyxy."""
    area1 = box_area(boxes1)                       # (N,)
    area2 = box_area(boxes2)                       # (M,)
    # Broadcast to compute the intersection rectangle for every (i,j) pair.
    lt = torch.max(boxes1[:, None, :2], boxes2[None, :, :2])   # (N,M,2) top-left
    rb = torch.min(boxes1[:, None, 2:], boxes2[None, :, 2:])   # (N,M,2) bot-right
    wh = (rb - lt).clamp(min=0)                                # (N,M,2)
    inter = wh[..., 0] * wh[..., 1]                            # (N,M)
    union = area1[:, None] + area2[None, :] - inter
    return inter / union.clamp(min=1e-9)

def match_anchors_to_targets(anchors, gt_boxes, gt_labels,
                             pos_iou=0.5, neg_iou=0.4, device=None):
    """Assign each anchor a label and a regression target.

    Returns:
      matched_labels : (N,) class id; 0=background, -1=ignore, >0 = class
      matched_boxes  : (N,4) the GT box each positive anchor must regress to
    """
    N = anchors.shape[0]
    if len(gt_boxes) == 0:
        return torch.zeros(N, dtype=torch.long, device=device), torch.zeros(N, 4, device=device)
    ious = box_iou(anchors, gt_boxes)                  # (N, G)
    best_iou, best_gt = ious.max(dim=1)                # best GT per anchor

    # Fix: Ensure matched_labels is created on the correct device
    matched_labels = torch.full((N,), -1, dtype=torch.long, device=device)   # start: ignore
    matched_labels[best_iou < neg_iou] = 0                    # background
    pos = best_iou >= pos_iou
    matched_labels[pos] = gt_labels[best_gt[pos]]

    # Guarantee every GT gets at least one positive anchor (its argmax anchor).
    best_anchor_per_gt = ious.argmax(dim=0)                   # (G,)
    # Fix: Ensure indices are on the correct device before assignment
    best_gt_indices = torch.arange(len(gt_boxes), device=device)
    matched_labels[best_anchor_per_gt] = gt_labels[best_gt_indices]
    best_gt[best_anchor_per_gt] = best_gt_indices

    matched_boxes = gt_boxes[best_gt]                          # (N,4)
    return matched_labels, matched_boxes

def detector_loss(cls_logits, box_deltas, anchors, targets,
                  pos_iou=0.5, neg_iou=0.4, neg_pos_ratio=3):
    """Compute the multi-task detection loss over a batch.

    cls_logits : (B, N, K)   box_deltas : (B, N, 4)
    anchors    : (N, 4)      targets    : list of B dicts(boxes, labels)
    """
    B, N, K = cls_logits.shape
    device = cls_logits.device
    total_cls = torch.tensor(0.0, device=device)
    total_reg = torch.tensor(0.0, device=device)
    n_pos_total = 0

    for b in range(B):
        gt_boxes  = targets[b]["boxes"].to(device)
        gt_labels = targets[b]["labels"].to(device)
        mlabels, mboxes = match_anchors_to_targets(
            anchors, gt_boxes, gt_labels, pos_iou, neg_iou, device=device)

        pos = mlabels > 0
        neg = mlabels == 0
        n_pos = int(pos.sum().item())
        n_pos_total += n_pos

        # --- classification loss with hard negative mining ---------------
        ce = F.cross_entropy(cls_logits[b], mlabels.clamp(min=0), reduction="none")
        ce_pos = ce[pos].sum()
        # Rank negatives by their loss; keep only the hardest (most-wrong) ones,
        # at neg_pos_ratio negatives per positive. We mask non-negatives to -1 so
        # topk can only ever pick genuine negative anchors.
        n_neg_keep = max(1, min(int(neg.sum().item()), neg_pos_ratio * max(n_pos, 1)))
        ce_masked = ce.clone()
        ce_masked[~neg] = -1.0
        hard_idx = ce_masked.topk(n_neg_keep).indices
        ce_neg = ce[hard_idx].sum()
        total_cls = total_cls + ce_pos + ce_neg

        # --- regression loss (positives only) ----------------------------
        if n_pos > 0:
            target_deltas = encode_boxes(mboxes[pos], anchors[pos])
            total_reg = total_reg + F.smooth_l1_loss(
                box_deltas[b][pos], target_deltas, reduction="sum")

    norm = max(n_pos_total, 1)
    cls_loss = total_cls / norm
    reg_loss = total_reg / norm
    return cls_loss + reg_loss, cls_loss.detach(), reg_loss.detach()

"""### 15.3 Inference: from raw outputs to clean detections

At test time we convert the network's dense per-anchor predictions into a short list of detections:

1. **Softmax** the class logits → per-class probabilities; drop the background class.
2. For each anchor, take its best non-background class & score.
3. **Threshold** by a minimum confidence to discard the sea of background predictions.
4. **Decode** the box deltas into absolute `xyxy` boxes (Section 11).
5. **Per-class NMS** (Section 12) to remove duplicates.

This `predict` function is what you'd call on a new image.
"""

@torch.no_grad()
def predict(model, images, score_thresh=0.3, nms_thresh=0.45, max_det=100):
    """Run the detector and return a list (per image) of dict(boxes,scores,labels)."""
    model.eval()
    images = images.to(DEVICE)
    cls_logits, box_deltas = model(images)
    probs = F.softmax(cls_logits, dim=-1)               # (B,N,K)
    anchors = model.anchors
    results = []
    for b in range(images.shape[0]):
        scores_all = probs[b][:, 1:]                    # drop background col
        best_score, best_cls = scores_all.max(dim=1)    # (N,)
        best_cls = best_cls + 1                          # shift back to 1..C
        keep = best_score > score_thresh
        if keep.sum() == 0:
            results.append({"boxes": torch.empty(0, 4),
                            "scores": torch.empty(0),
                            "labels": torch.empty(0, dtype=torch.long)})
            continue
        boxes = decode_boxes(box_deltas[b][keep], anchors[keep])
        boxes[:, [0, 2]] = boxes[:, [0, 2]].clamp(0, IMG_SIZE)   # clip to image
        boxes[:, [1, 3]] = boxes[:, [1, 3]].clamp(0, IMG_SIZE)
        scores = best_score[keep]; labels = best_cls[keep]
        k = batched_nms(boxes.cpu(), scores.cpu(), labels.cpu(), nms_thresh)[:max_det]
        results.append({"boxes": boxes.cpu()[k], "scores": scores.cpu()[k],
                        "labels": labels.cpu()[k]})
    return results

