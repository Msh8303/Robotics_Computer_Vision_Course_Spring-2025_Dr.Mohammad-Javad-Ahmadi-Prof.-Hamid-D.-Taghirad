"""### Matching anchors to ground truth (label assignment)

During training we must decide, for each anchor, *what it should predict*. This **assignment** step is one of the most important — and most varied — design choices in detection. The classic IoU-based rule (Faster R-CNN / SSD) is:

- Compute IoU between every anchor and every GT box.
- An anchor is a **positive** (assigned to a GT) if its IoU with that GT ≥ a high threshold (e.g. 0.5).
- An anchor is a **negative** (background) if its best IoU < a low threshold (e.g. 0.4).
- Anchors in between are **ignored** (don't contribute to the loss) to avoid confusing the model.
- **Guarantee:** each GT is assigned to at least its single best-matching anchor, so no object is left unrepresented even if no anchor clears the threshold.

Positives get a box-regression target (the encoded delta to their GT) **and** a class label; negatives get the *background* class and **no** box target. Let's implement and visualize this matcher — the bridge between data and loss.
"""

def match_anchors_to_targets(anchors, gt_boxes, gt_labels,
                             pos_iou=0.5, neg_iou=0.4):
    """Assign each anchor a label and a regression target.

    Returns:
      matched_labels : (N,) class id; 0=background, -1=ignore, >0 = class
      matched_boxes  : (N,4) the GT box each positive anchor must regress to
    """
    N = anchors.shape[0]
    if len(gt_boxes) == 0:
        return torch.zeros(N, dtype=torch.long), torch.zeros(N, 4)
    ious = box_iou(anchors, gt_boxes)                  # (N, G)
    best_iou, best_gt = ious.max(dim=1)                # best GT per anchor

    matched_labels = torch.full((N,), -1, dtype=torch.long)   # start: ignore
    matched_labels[best_iou < neg_iou] = 0                    # background
    pos = best_iou >= pos_iou
    matched_labels[pos] = gt_labels[best_gt[pos]]

    # Guarantee every GT gets at least one positive anchor (its argmax anchor).
    best_anchor_per_gt = ious.argmax(dim=0)                   # (G,)
    matched_labels[best_anchor_per_gt] = gt_labels
    best_gt[best_anchor_per_gt] = torch.arange(len(gt_boxes))

    matched_boxes = gt_boxes[best_gt]                          # (N,4)
    return matched_labels, matched_boxes

