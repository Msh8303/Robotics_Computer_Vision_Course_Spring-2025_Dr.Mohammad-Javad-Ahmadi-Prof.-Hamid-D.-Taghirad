"""---
# Part II — The Two-Stage Era

Now that we can represent boxes, measure overlap, generate data, and evaluate detectors, we trace the *historical and conceptual* evolution of detection. Understanding *why* each method was invented — by seeing the flaw in its predecessor — is the fastest route to deep understanding.

## 7. The naive approach: sliding windows & why they fail

Before deep learning, and as a first idea anyone would have, detection was framed as **"classification at every location and scale."** The recipe:

1. Slide a fixed-size window across the image at every position.
2. Repeat at multiple scales (an image pyramid) to catch objects of different sizes.
3. Run a classifier on each window crop. Where it fires "object," emit a box.

This is exactly how the classic Viola–Jones face detector (2001) and HOG+SVM pedestrian detectors worked. Let's *count* how many windows this implies, because the number is the whole story.
"""

def count_sliding_windows(img_size=128, window_sizes=(16, 32, 48, 64), stride=4):
    total = 0
    for ws in window_sizes:
        positions = ((img_size - ws) // stride + 1) ** 2   # x * y positions
        total += positions
        print(f"  window {ws:>3}px, stride {stride}: {positions:>6,} positions")
    print(f"TOTAL windows to classify: {total:,}")
    return total

print("For a tiny 128x128 image:")
n = count_sliding_windows()
print(f"\nA real 1000x1000 image with finer scales/strides easily exceeds millions.")
print("Each window needs a full CNN forward pass => computationally hopeless,")
print("and we still haven't handled aspect ratios (tall vs wide objects).")

"""**The two fatal problems with sliding windows:**

1. **Combinatorial explosion.** Even our toy image needs tens of thousands of windows; a real image needs millions, each requiring a CNN forward pass. Far too slow.
2. **Scale & aspect ratio.** A fixed window can't tightly fit a tall person *and* a wide bus. You'd need many window shapes, multiplying the cost further.

Every architecture in this notebook is, in essence, a clever way to **avoid classifying millions of windows** while still considering many candidate locations. Keep that framing — it unifies the entire field.

## 8. R-CNN (2014) — *Regions with CNN features*

**Key idea:** Don't look at *every* window. Instead, use a cheap algorithm to propose a few thousand **regions that probably contain objects** ("region proposals"), then run the expensive CNN classifier on only those.

R-CNN's pipeline (Girshick et al., 2014) — the paper that ignited deep-learning detection:

1. **Region proposals (~2000)** via *Selective Search* — a classical algorithm that merges similar adjacent pixels into candidate object regions. It's *class-agnostic*: it just finds "blobby" things.
2. **Warp** each proposed region to a fixed size (e.g. 224×224).
3. **CNN feature extraction** — run each warped region through a CNN (e.g. AlexNet) to get a feature vector.
4. **Classify** each region with per-class SVMs, and
5. **Refine** the box with a class-specific linear **bounding-box regressor**.

This was a massive accuracy leap (mAP on VOC jumped ~30% → ~58%). But it has glaring inefficiencies:

- **Painfully slow:** ~2000 CNN forward passes *per image* (the crops overlap heavily, so computation is hugely redundant). ~47s/image at test time.
- **Multi-stage, not end-to-end:** CNN, SVMs, and regressors are trained separately — awkward and suboptimal.
- **Selective Search is fixed** (not learned) and itself slow.

We won't implement Selective Search (it's classical CV), but we'll *emulate the concept* — proposals, warping, then classify-and-refine — so the mechanics are concrete. Below, we simulate proposals as a noisy set of candidate boxes and illustrate the warp-then-classify step.
"""

def simulate_region_proposals(img_size=128, n=40, gt_boxes=None, rng=None):
    """Stand-in for Selective Search: class-agnostic candidate boxes.
    Real Selective Search merges similar pixels, so proposals cluster around
    real objects. We emulate that by mixing random boxes with jittered
    object-centered boxes — the *interface* (a list of candidates) is identical,
    which is all that matters conceptually."""
    rng = rng or np.random
    boxes = []
    # ~40% purely random (clutter), rest seeded near objects (the realistic part)
    n_random = int(n * 0.4)
    for _ in range(n_random):
        x1 = rng.randint(0, img_size - 20); y1 = rng.randint(0, img_size - 20)
        w  = rng.randint(16, 50); h = rng.randint(16, 50)
        boxes.append([x1, y1, min(img_size, x1 + w), min(img_size, y1 + h)])
    if gt_boxes is not None and len(gt_boxes):
        gb = gt_boxes.numpy() if torch.is_tensor(gt_boxes) else np.asarray(gt_boxes)
        per = max(1, (n - n_random) // len(gb))
        for g in gb:
            for _ in range(per):
                jx, jy = rng.randint(-6, 7, 2)
                jw, jh = rng.randint(-6, 7, 2)
                x1 = max(0, g[0] + jx); y1 = max(0, g[1] + jy)
                x2 = min(img_size, g[2] + jw); y2 = min(img_size, g[3] + jh)
                if x2 - x1 >= 6 and y2 - y1 >= 6:
                    boxes.append([x1, y1, x2, y2])
    return torch.tensor(boxes, dtype=torch.float32)

