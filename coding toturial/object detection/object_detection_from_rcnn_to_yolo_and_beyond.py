"""## 10. Faster R-CNN (2015) — learn the proposals too

**The last bottleneck:** Fast R-CNN still used **Selective Search**, a slow, fixed, non-learned algorithm, to generate proposals. It dominated runtime (~2s/image).

**Faster R-CNN's breakthrough (Ren et al., 2015):** replace Selective Search with a small neural network — the **Region Proposal Network (RPN)** — that *learns* to propose regions directly from the shared feature map. Now proposals are learned, fast, and the whole system is (nearly) end-to-end. This architecture defined the **two-stage detector** template that dominated for years.

### The two stages

```
Image
  │
  ▼
[ Backbone CNN ]  ── shared conv feature map ──┐
  │                                            │
  ▼                                            ▼
[ Stage 1: RPN ]                      [ Stage 2: RoI head ]
 proposes ~300 class-agnostic   →     for each proposal: RoIAlign,
 "object/not-object" boxes            then classify (C classes) +
 + coarse box refinement              refine box precisely
```

- **Stage 1 (RPN):** a tiny conv head slides over the feature map. At each location it considers **anchors** — a set of reference boxes of predefined scales and aspect ratios — and predicts (a) an *objectness* score and (b) a refinement of each anchor. Outputs the top few hundred proposals.
- **Stage 2 (RoI head):** RoIAlign each proposal from the shared map, then two sibling heads predict the *class* (now including which of the C categories) and a *finer* box adjustment.

### Why "anchors"?

Anchors are the conceptual key that the single-stage detectors (YOLO, SSD) will also adopt, so we'll devote the next section entirely to them. The short version: instead of regressing a box from nothing, the network refines a fixed *reference box*. Predicting small offsets to a sensible prior is far easier than predicting absolute coordinates, and a *grid of anchors of several shapes* covers all positions/scales/aspect-ratios cheaply — finally solving the scale/aspect-ratio problem that killed sliding windows.

Rather than reimplement the whole of Faster R-CNN here (we'll build a complete single-stage detector from scratch shortly, which shares most concepts), let's load torchvision's pretrained Faster R-CNN and dissect its structure to make the two-stage anatomy concrete.
"""

