"""## 9. Fast R-CNN (2015) — share the convolution

**The flaw R-CNN had:** running the CNN ~2000× per image, recomputing convolutions on overlapping crops.

**Fast R-CNN's fix (Girshick, 2015):** run the convolutional backbone **once** on the whole image to get a feature map. Since convolution preserves spatial layout, each region proposal corresponds to a *sub-rectangle of the feature map*. Extract a fixed-size feature for each proposal directly from that shared map with **RoI Pooling**, then classify and regress. Now the heavy conv work is shared across all proposals.

### RoI Pooling

A proposal (in image coordinates) is projected onto the feature map (scaled by the backbone's stride). That feature sub-window has an arbitrary size, but the classifier head needs a **fixed-size** input (say 7×7). **RoI Pooling** divides the projected region into a fixed grid (7×7) and max-pools within each cell — turning *any* region into a 7×7 feature, regardless of its original size.

> Fast R-CNN also unified training into a **single multi-task loss** (classification + box regression together) and was ~**200× faster** at test time than R-CNN. Its remaining bottleneck: it *still* relied on slow external Selective Search proposals.

Let's implement a simple RoI pooling and *see* it convert differently-sized regions to a common shape. (Torchvision provides the optimized `roi_pool`/`roi_align`; implementing the idea ourselves makes it click.)
"""

def roi_pool_single(feature_map, roi_xyxy, output_size=7, spatial_scale=1.0):
    """Max-pool an RoI from a (C,H,W) feature map into (C, output_size, output_size).

    spatial_scale maps image coords -> feature-map coords (= 1/backbone_stride).
    This is a clear, readable version of the operation in the Fast R-CNN paper.
    """
    C, H, W = feature_map.shape
    x1, y1, x2, y2 = (roi_xyxy * spatial_scale)
    x1, y1 = int(x1.floor()), int(y1.floor())
    x2, y2 = int(x2.ceil()),  int(y2.ceil())
    x1, y1 = max(0, x1), max(0, y1)
    x2, y2 = min(W, max(x1 + 1, x2)), min(H, max(y1 + 1, y2))
    roi = feature_map[:, y1:y2, x1:x2]                 # (C, h, w)
    # F.adaptive_max_pool2d does exactly the "fixed-grid max-pool" we want.
    return F.adaptive_max_pool2d(roi[None], output_size)[0]

