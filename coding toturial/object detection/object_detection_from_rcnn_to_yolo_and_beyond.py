"""---
# Part IV — Modern Detection & Beyond

Our from-scratch detector uses a **single** feature scale. Real detectors must handle objects spanning a huge range of sizes (a distant pedestrian vs. a nearby bus). Part IV covers the ideas that make modern detectors accurate and robust, then surveys where the field has gone.

## 16. Feature Pyramid Networks (FPN, 2017)

**The problem.** Deep CNNs build a natural feature hierarchy: early layers are high-resolution with weak semantics (good for *localizing small* objects, bad at *recognizing*); deep layers are low-resolution with strong semantics (great at recognizing, but too coarse to localize small things). SSD attached heads to multiple layers, but the shallow layers it used for small objects had weak features.

**The FPN idea (Lin et al.).** Build a feature *pyramid* where **every level has strong semantics**. Take the deep, semantic-rich feature map and progressively **upsample** it, **adding** (lateral connections) the corresponding higher-resolution map from the backbone at each step. The result: a set of feature maps at multiple resolutions that are *all* semantically strong. Attach a detection head to each.

```
Backbone (bottom-up)        FPN (top-down + lateral)
   C5  (8x8, deep)  ───1x1──►  P5  (8x8)  ── head ► large objects
    │                            │ upsample x2 + add
   C4  (16x16)      ───1x1──►  P4  (16x16) ── head ► medium objects
    │                            │ upsample x2 + add
   C3  (32x32)      ───1x1──►  P3  (32x32) ── head ► small objects
```

FPN became a near-universal *neck* — Faster R-CNN, RetinaNet, Mask R-CNN, and many YOLO variants all use it or a descendant (PANet, BiFPN). Let's implement a compact FPN and confirm the output pyramid shapes.
"""

class FPN(nn.Module):
    """Minimal Feature Pyramid Network with 3 levels.
    Takes 3 backbone maps (C3,C4,C5) -> 3 enriched maps (P3,P4,P5), all with
    the same channel count, each semantically strong."""
    def __init__(self, in_channels_list, out_channels=128):
        super().__init__()
        # 1x1 "lateral" convs unify channel counts.
        self.lateral = nn.ModuleList(
            [nn.Conv2d(c, out_channels, 1) for c in in_channels_list])
        # 3x3 "output" convs smooth the merged maps (reduce upsampling aliasing).
        self.output = nn.ModuleList(
            [nn.Conv2d(out_channels, out_channels, 3, padding=1)
             for _ in in_channels_list])
    def forward(self, feats):                      # feats = [C3, C4, C5]
        laterals = [l(f) for l, f in zip(self.lateral, feats)]
        # Top-down pathway: start at the deepest, add upsampled into shallower.
        for i in range(len(laterals) - 1, 0, -1):
            up = F.interpolate(laterals[i], size=laterals[i-1].shape[-2:],
                               mode="nearest")
            laterals[i-1] = laterals[i-1] + up
        return [o(l) for o, l in zip(self.output, laterals)]

class BackboneWithFPN(nn.Module):
    """Expose C3,C4,C5 from our backbone and pass them through an FPN."""
    def __init__(self, width=32, fpn_channels=128):
        super().__init__()
        bb = Backbone(width)
        self.stem, self.layer1 = bb.stem, bb.layer1
        self.layer2, self.layer3, self.layer4 = bb.layer2, bb.layer3, bb.layer4
        self.fpn = FPN([width*4, width*4, width*4], fpn_channels)
        self.out_channels = fpn_channels
    def forward(self, x):
        x = self.stem(x); x = self.layer1(x)
        c3 = self.layer2(x)        # 32x32
        c4 = self.layer3(c3)       # 16x16
        c5 = self.layer4(c4)       # 8x8
        return self.fpn([c3, c4, c5])    # [P3(32),P4(16),P5(8)]

_bbfpn = BackboneWithFPN().to(DEVICE)
_p = _bbfpn(torch.randn(1, 3, IMG_SIZE, IMG_SIZE, device=DEVICE))
print("FPN pyramid levels (each semantically strong):")
for i, f in enumerate(_p):
    print(f"  P{i+3}: {tuple(f.shape)}  -> detects "
          f"{'small' if i==0 else 'medium' if i==1 else 'large'} objects")
print("\nTo make a multi-scale detector: place anchors + a shared head on EACH "
      "level, with smaller anchors on P3 and larger anchors on P5.")

"""## 17. Class imbalance & Focal Loss (RetinaNet, 2017)

We met this problem head-on while training our detector: of ~576 anchors, only ~6 are positive — a **~100:1 background-to-foreground imbalance**. Two-stage detectors dodge it (the RPN filters to ~300 proposals before classification). Single-stage detectors evaluate *all* anchors densely, so the loss is swamped by easy negatives. We patched it with **hard-negative mining**; **RetinaNet** introduced a more elegant fix: **Focal Loss**.

**The insight.** Even correctly-classified easy negatives (background the model is already sure about) each contribute a small loss, but there are *so many* that they collectively dominate and drown out the rare, informative positives. Focal Loss *down-weights easy examples* so training focuses on hard ones.

Standard cross-entropy for the true class with predicted probability $p_t$:

$$\text{CE}(p_t) = -\log(p_t)$$

Focal Loss adds a **modulating factor** $(1 - p_t)^\gamma$:

$$\text{FL}(p_t) = -\alpha_t (1 - p_t)^{\gamma} \log(p_t)$$

- When an example is **easy** ($p_t \to 1$), $(1-p_t)^\gamma \to 0$ — its loss is suppressed.
- When **hard** ($p_t$ small), the factor $\to 1$ — full loss, full attention.
- $\gamma$ (typically 2) controls the strength; $\gamma{=}0$ recovers plain CE. $\alpha$ balances pos/neg.

This lets RetinaNet train on *all* anchors with no mining and still beat two-stage accuracy. Let's implement it and plot how it reshapes the loss.
"""

def focal_loss(logits, targets, alpha=0.25, gamma=2.0, reduction="sum"):
    """Multi-class focal loss via per-class sigmoid (RetinaNet style).

    logits  : (N, C) raw scores for C foreground classes
    targets : (N, C) multi-hot {0,1} (all zeros = background)
    """
    p = torch.sigmoid(logits)
    ce = F.binary_cross_entropy_with_logits(logits, targets, reduction="none")
    p_t = p * targets + (1 - p) * (1 - targets)            # prob of true label
    modulating = (1 - p_t) ** gamma
    alpha_t = alpha * targets + (1 - alpha) * (1 - targets)
    loss = alpha_t * modulating * ce
    return loss.sum() if reduction == "sum" else loss.mean()

# Visualize the modulating factor's effect: loss vs. predicted prob of true class.
pt = torch.linspace(0.01, 0.999, 200)
ce_loss = -torch.log(pt)
fig, ax = plt.subplots(figsize=(8, 5))
for g in [0, 0.5, 1, 2, 5]:
    fl = ((1 - pt) ** g) * (-torch.log(pt))
    ax.plot(pt, fl, label=f"γ = {g}" + (" (= CE)" if g == 0 else ""), lw=2)
ax.axvspan(0.6, 1.0, alpha=0.08, color="green")
ax.text(0.78, ax.get_ylim()[1]*0.6, "easy examples\n(well-classified)",
        ha="center", color="green")
ax.set_xlabel("probability of true class $p_t$"); ax.set_ylabel("loss")
ax.set_title("Focal Loss down-weights easy examples (higher γ = stronger)")
ax.legend(); ax.grid(alpha=0.3); ax.set_ylim(0, 5)
plt.tight_layout(); plt.show()
print("Notice: at p_t=0.9 (easy), γ=2 gives ~100x less loss than CE.")
print("Hard examples (low p_t) keep nearly full loss => the model focuses there.")

"""**Practical note.** RetinaNet also uses the **prior-bias initialization** we applied in Section 15.1 — initializing the final classification bias so the model starts by predicting foreground probability ≈ 0.01. Without it, the enormous number of background anchors produces a huge, destabilizing loss in the first iterations. (That same init is precisely what rescued our from-scratch detector from collapse — a concrete instance of theory mattering in practice.)

You could swap our `detector_loss`'s hard-negative-mining classification term for `focal_loss` and remove the mining entirely; the detector would train on all anchors. This is left as a natural exercise — every piece you'd need is already defined.

## 18. YOLO's evolution & the anchor-free turn

YOLO became a family, each version absorbing the best ideas of its time. A conceptual timeline (not exhaustive — focus on the *ideas*):

| Version | Year | Key contributions |
|---|---|---|
| **YOLOv1** | 2016 | Single-pass grid regression; real-time but coarse. |
| **YOLOv2 / YOLO9000** | 2017 | **Anchor boxes** (clustered from data via k-means), batch-norm, higher resolution, multi-scale training. |
| **YOLOv3** | 2018 | **Multi-scale prediction** (3 scales, FPN-like), better backbone (Darknet-53), independent logistic class predictions. |
| **YOLOv4 / v5** | 2020 | Bag-of-tricks: **Mosaic augmentation**, CIoU loss, PANet neck, CSP backbones; v5 brought a polished PyTorch ecosystem. |
| **YOLOX** | 2021 | **Anchor-free**, decoupled head, **SimOTA** dynamic label assignment. |
| **YOLOv6 / v7** | 2022 | Reparameterizable blocks, auxiliary heads, efficiency-focused designs. |
| **YOLOv8** | 2023 | Anchor-free, unified API for detect/segment/pose/classify. |
| **YOLOv9 / v10 / v11+** | 2024+ | Programmable gradient info, **NMS-free** training (v10), continued efficiency/accuracy gains. |

> Versions and benchmark numbers move quickly. Treat any specific "best model" claim as time-sensitive and verify against current sources before relying on it.

Two themes dominate this evolution and are worth understanding deeply:

### Better label assignment
Early detectors used a fixed IoU rule (Section 11). Modern detectors use **dynamic / learned assignment** — letting the *quality of predictions* decide which anchors/points are positive (e.g. ATSS picks a per-object IoU threshold from statistics; **OTA/SimOTA** frames assignment as an optimal-transport problem; TaskAligned assignment couples classification and localization quality). Better assignment often buys more accuracy than architecture tweaks.

### The anchor-free turn
Anchors carry baggage: hyperparameters (scales, ratios), many boxes, and a foreground/background imbalance. **Anchor-free** detectors drop them:

- **FCOS** (2019): for each *feature-map pixel* inside an object, directly regress the **4 distances** to the object's top/left/bottom/right edges, plus a "**center-ness**" score that down-weights low-quality boxes far from object centers. Per-pixel, per-FPN-level prediction.
- **CenterNet** (2019): represent each object by its **center point** as a peak in a heatmap, then regress size & offset at that peak. Detection becomes keypoint estimation; NMS is often unnecessary.

Let's make the FCOS idea concrete: visualize the "distance to four edges" regression target for points inside an object.
"""

# Illustrate FCOS-style per-point targets on a synthetic image.
_rng = np.random.RandomState(11)
