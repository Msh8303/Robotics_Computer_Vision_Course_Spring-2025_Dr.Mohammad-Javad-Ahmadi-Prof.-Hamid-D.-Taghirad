"""### NMS variants worth knowing

- **Soft-NMS:** instead of deleting overlapping boxes, *decay* their scores by a function of the overlap. Helps when objects genuinely overlap (e.g. a crowd), where hard NMS would wrongly delete a real neighbor.
- **Class-agnostic NMS:** suppress across all classes at once (sometimes used when you expect one object to get only one label).
- **The threshold is a real knob:** too low → deletes true neighbors (low recall in crowds); too high → leaves duplicates (low precision). Typical values: 0.45–0.5 (SSD), 0.7 (RPN proposals).
- **NMS-free detectors:** DETR (Section 19) avoids NMS entirely via set prediction with bipartite matching — a major conceptual shift.

## 13. YOLO (v1, 2016) — detection as a single regression

**YOLO** ("You Only Look Once", Redmon et al.) made a radical simplification: *no proposals, no per-region classification — one network, one pass, predicting all boxes and classes simultaneously.*

YOLOv1's formulation:

- Divide the image into an $S \times S$ grid (e.g. 7×7).
- **The cell that contains an object's center is responsible for detecting it.**
- Each cell predicts $B$ boxes (each: $x, y, w, h$, confidence) **and** $C$ class probabilities.
- Output tensor: $S \times S \times (B \cdot 5 + C)$ — a single regression target.

Trained with a sum-squared-error loss (with clever weighting: more weight on coordinate errors, less on the many empty cells' confidence). The payoff was **speed** — real-time detection (45 FPS) — versus the seconds-per-image of two-stage methods.

**YOLOv1's weaknesses** (which later versions and SSD fixed): struggles with small objects and groups (each cell predicts limited boxes), coarse localization, and only one aspect-ratio sense per cell. Still, the "single-shot dense prediction" idea it pioneered underlies essentially all fast detectors today.

The conceptual leap: **detection = a fixed-size tensor regression**, where structure (the grid) handles the "variable number of objects" problem — most cells simply predict "no object."

## 14. SSD (2016) — multi-scale default boxes

**SSD** ("Single Shot MultiBox Detector", Liu et al.) combined YOLO's single-pass speed with anchors (called *default boxes*) and a crucial idea: **predict from multiple feature maps at different resolutions.**

Key ideas:

- **Multi-scale feature maps:** attach detection heads to several layers of the backbone. *Early, high-resolution* maps (large, fine grid) detect **small** objects; *deep, low-resolution* maps (small, coarse grid, large receptive field) detect **large** objects. This directly tackles the scale problem.
- **Default boxes (anchors)** of several aspect ratios at each location of each feature map — exactly the anchors we built in Section 11.
- **Per-location prediction** of class scores + box offsets for each default box, in one pass.
- **Hard negative mining** during training: since the vast majority of default boxes are background, SSD keeps only the hardest (highest-loss) negatives at a fixed 3:1 negative:positive ratio — an early, simple answer to class imbalance (Focal Loss, Section 17, is the more elegant one).

The multi-scale prediction is the lasting lesson. We'll borrow a *light* version of it conceptually, then build a single-scale detector for clarity (multi-scale is a straightforward extension once the single-scale machinery is understood).

The diagram below contrasts the three paradigms we've covered.
"""

