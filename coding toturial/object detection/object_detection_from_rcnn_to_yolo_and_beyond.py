"""### Wrapping it in a PyTorch `Dataset`

We wrap the generator in a `torch.utils.data.Dataset`. Two design points worth noting because they're universal in detection:

1. **Image → `CHW` float tensor.** PyTorch convolutions expect channels-first.
2. **A custom `collate_fn`.** Because each image has a *different number* of boxes, you **cannot** stack targets into a single tensor the way you stack images. The standard solution is a `collate_fn` that stacks images but keeps targets as a *list* of per-image dicts. This pattern is exactly what torchvision's detection API uses.
"""

class ShapesDataset(Dataset):
    """On-the-fly synthetic detection dataset. No files needed."""
    def __init__(self, length=2000, max_objects=4, img_size=IMG_SIZE, seed=0):
        self.length = length
        self.max_objects = max_objects
        self.img_size = img_size
        self.seed = seed

    def __len__(self):
        return self.length

    def __getitem__(self, idx):
        # Deterministic per-index RNG => the "same" dataset every epoch,
        # which makes training curves reproducible. Bump seed for a new split.
        rng = np.random.RandomState(self.seed * 100003 + idx)
        img, boxes, labels = generate_image(self.max_objects, self.img_size, rng=rng)
        img = torch.from_numpy(img).permute(2, 0, 1).contiguous()   # CHW
        target = {
            "boxes":  torch.from_numpy(boxes),                       # (N,4) xyxy px
            "labels": torch.from_numpy(labels),                      # (N,)
        }
        return img, target

def detection_collate(batch):
    """Stack images; keep targets as a list (variable #objects per image)."""
    images = torch.stack([b[0] for b in batch], dim=0)
    targets = [b[1] for b in batch]
    return images, targets

