def warp_region(image_chw, box, out_size=64):
    """Crop a box from a CHW image and resize to out_size x out_size."""
    x1, y1, x2, y2 = [int(v) for v in box]
    x1, y1 = max(0, x1), max(0, y1)
    crop = image_chw[:, y1:y2, x1:x2]
    if crop.numel() == 0:
        return torch.zeros(image_chw.shape[0], out_size, out_size)
    return F.interpolate(crop[None], size=(out_size, out_size),
                         mode="bilinear", align_corners=False)[0]

