"""## 4. Intersection over Union (IoU) — the heart of detection

**IoU** measures how much two boxes overlap. It is *the* fundamental quantity in detection, used for:

- **Matching** predicted/anchor boxes to ground-truth boxes during training.
- **Non-Maximum Suppression** (removing duplicate detections).
- **Evaluation** (a detection counts as correct only if its IoU with a true box exceeds a threshold).

Given two boxes $A$ and $B$:

$$\text{IoU}(A,B) = \frac{|A \cap B|}{|A \cup B|} = \frac{\text{area of intersection}}{\text{area of union}}$$

It ranges from **0** (no overlap) to **1** (perfect overlap). The intersection rectangle has corners:

$$x_1 = \max(A_{x_1}, B_{x_1}),\quad y_1 = \max(A_{y_1}, B_{y_1})$$
$$x_2 = \min(A_{x_2}, B_{x_2}),\quad y_2 = \min(A_{y_2}, B_{y_2})$$

with width $\max(0, x_2 - x_1)$ and height $\max(0, y_2 - y_1)$ (the `max(0, ·)` handles the no-overlap case). And:

$$|A \cup B| = |A| + |B| - |A \cap B|$$

We implement a **vectorized** version computing IoU between *every* box in set 1 and *every* box in set 2 — an `(N, M)` matrix — because that's what training and NMS need. Doing this with broadcasting (not Python loops) is essential for speed.
"""

def box_area(boxes: torch.Tensor) -> torch.Tensor:
    """Area of xyxy boxes. boxes: (..., 4)."""
    return (boxes[..., 2] - boxes[..., 0]).clamp(min=0) * \
           (boxes[..., 3] - boxes[..., 1]).clamp(min=0)

def box_iou(boxes1: torch.Tensor, boxes2: torch.Tensor) -> torch.Tensor:
    """Pairwise IoU. boxes1:(N,4) boxes2:(M,4) -> (N,M). Both xyxy."""
    area1 = box_area(boxes1)                       # (N,)
    area2 = box_area(boxes2)                       # (M,)
    # Broadcast to compute the intersection rectangle for every (i,j) pair.
    lt = torch.max(boxes1[:, None, :2], boxes2[None, :, :2])   # (N,M,2) top-left
    rb = torch.min(boxes1[:, None, 2:], boxes2[None, :, 2:])   # (N,M,2) bot-right
    wh = (rb - lt).clamp(min=0)                                # (N,M,2)
    inter = wh[..., 0] * wh[..., 1]                            # (N,M)
    union = area1[:, None] + area2[None, :] - inter
    return inter / union.clamp(min=1e-9)

# --- Verify against hand-computed values --------------------------------
