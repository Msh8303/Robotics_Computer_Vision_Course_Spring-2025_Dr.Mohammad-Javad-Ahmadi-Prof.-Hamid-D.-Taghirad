"""## 19. DETR (2020) — detection as direct set prediction

Every detector so far ends with **NMS** to clean up duplicate predictions — a hand-crafted, non-differentiable post-process. **DETR** ("DEtection TRansformer", Carion et al.) eliminated it, reframing detection as **direct set prediction**: output a *set* of exactly $N$ predictions (e.g. 100), and train so the set matches the ground-truth set — no anchors, no NMS.

### The architecture
1. A CNN backbone extracts features.
2. A **Transformer encoder** processes the flattened feature map (self-attention lets every location attend globally — great for reasoning about object relationships and large objects).
3. A **Transformer decoder** takes $N$ learned **object queries** and, attending to the encoded image, turns each into one prediction (class + box). Each query learns to specialize (e.g. to a region/size).

### The key trick: bipartite matching loss
With $N$ predictions and (say) 3 real objects, how do we assign predictions to ground truth to compute a loss? DETR finds the **optimal one-to-one assignment** between predictions and GT (padded with "no object") via the **Hungarian algorithm**, minimizing a matching cost (class + box error). Each GT is matched to exactly one prediction; the rest must predict "no object." Because the assignment is one-to-one, **duplicates are penalized during training** — so no NMS is needed at test time.

Let's implement the Hungarian matching on a toy cost matrix to demystify it (SciPy ships with Colab), and visualize the one-to-one assignment.
"""

from scipy.optimize import linear_sum_assignment

# Toy: 5 predictions vs 3 ground-truth objects. Build a cost = how badly each
# prediction matches each GT (lower = better). DETR uses class + L1 + GIoU cost.
torch.manual_seed(0)
pred_boxes = torch.rand(5, 4)                          # normalized cxcywh-ish
gt_boxes   = torch.rand(3, 4)
# Simple L1 cost between every (pred, gt) pair -> (5,3) cost matrix.
cost = torch.cdist(pred_boxes, gt_boxes, p=1).numpy()
row_ind, col_ind = linear_sum_assignment(cost)         # Hungarian: optimal 1-to-1
print("Cost matrix (preds x gts):\n", cost.round(2))
print("\nOptimal assignment (prediction -> ground truth):")
for r, c in zip(row_ind, col_ind):
    print(f"  prediction {r}  matched to  GT {c}   (cost {cost[r, c]:.2f})")
unmatched = sorted(set(range(5)) - set(row_ind))
print(f"  predictions {unmatched} -> trained to output 'no object'")
print("\nOne GT gets exactly one prediction => no duplicates => no NMS at test.")

"""### DETR's trade-offs & descendants

DETR is conceptually beautiful (end-to-end, NMS-free, set-based) but the original had drawbacks: **slow convergence** (needed ~500 epochs) and weaker small-object performance. Follow-ups fixed these and made Transformer detectors state-of-the-art:

- **Deformable DETR** — sparse attention over a few sampled points (faster convergence, better small objects).
- **DINO / DN-DETR** — denoising training and improved query design; among the strongest detectors today.
- **RT-DETR** — a real-time Transformer detector competitive with YOLO on speed.

DETR also unified detection with segmentation and other tasks under one set-prediction paradigm, influencing the broader move toward Transformer-based vision. The big takeaway: **detection can be learned end-to-end as set prediction**, dissolving the hand-engineered components (anchors, NMS) one by one.

## 20. Practical tips, tricks & common pitfalls

The gap between a textbook detector and one that *works on your data* is filled with practical knowledge. This section distills the field's hard-won lessons.

### Data augmentation (often the single biggest lever)
- **Geometric:** random horizontal flip, scale jitter, crops, rotations — when you flip an image you **must flip the boxes too** (a classic bug). Translation/scale teaches scale/position invariance.
- **Photometric:** brightness, contrast, hue, saturation jitter — robustness to lighting.
- **Mosaic** (YOLOv4+): stitch 4 images into one, exposing the model to many scales/contexts per sample and effectively enlarging batch diversity.
- **MixUp / CutMix / Copy-Paste:** blend images or paste object instances onto new backgrounds — strong gains, especially for instance segmentation and rare classes.

### Training recipe
- **Transfer learning is standard:** start from an ImageNet- (or COCO-) pretrained backbone. Training a detector backbone from scratch needs far more data. (Our synthetic task is simple enough to train from scratch — real tasks rarely are.)
- **Learning-rate warmup** for the first few hundred iterations, then a **cosine** or **step** decay. Detection losses are unstable early (the imbalance problem); warmup tames the first steps.
- **Freeze BatchNorm** statistics when fine-tuning with small batches (small-batch BN is noisy); or use GroupNorm.
- **Multi-scale training:** randomly resize inputs each iteration so the model sees objects at many scales.
- **EMA of weights:** keep an exponential moving average of parameters for evaluation — usually a free accuracy bump.

### Anchor / assignment design
- **Match anchors to your data.** If your objects are tall and thin, default square anchors waste capacity. **k-means your GT box sizes** to choose anchor scales/ratios (YOLOv2's trick). Always *check anchor coverage*: what's the best IoU each GT can achieve? (We did exactly this when debugging.)
- Prefer **modern assignment** (ATSS / SimOTA / TaskAligned) over a fixed IoU threshold if accuracy matters.

### Inference & evaluation
- **Tune the NMS IoU threshold and score threshold** on a validation set — they trade precision against recall.
- For crowded scenes, consider **Soft-NMS**.
- **Always report COCO mAP@[.5:.95]**, not just mAP@0.5 — the latter hides poor localization.
- **Test-Time Augmentation (TTA):** run on flips/scales and merge — slower but more accurate.

### Common pitfalls (a debugging checklist)
1. **Coordinate-format mismatch** (`xyxy` vs `cxcywh` vs normalized vs pixels) — the #1 bug. Assert your formats.
2. **Forgetting to transform boxes with the image** during augmentation.
3. **Loss collapse to background** — fix with prior-bias init, focal loss, or hard-negative mining (we hit and fixed this exact issue).
4. **Anchors that don't cover your objects' sizes** — silently caps achievable recall.
5. **Class indices off-by-one** — background usually index 0; foreground starts at 1.
6. **Evaluating with the wrong score threshold** — too high crushes recall and your mAP looks artificially low.
7. **Train/val leakage** — ensure splits are genuinely disjoint (we used disjoint RNG seeds).

Below: a quick, correct **augmentation** demo showing image *and* box transforms staying in sync — the thing people most often get wrong.
"""

def augment(image_chw, boxes, img_size=IMG_SIZE, rng=None):
    """Random horizontal flip + brightness jitter, keeping boxes consistent."""
    rng = rng or np.random
    img = image_chw.clone()
    boxes = boxes.clone()
    # Horizontal flip: mirror pixels AND mirror box x-coordinates.
    if rng.rand() < 0.5:
        img = torch.flip(img, dims=[2])                # flip width axis
        x1 = boxes[:, 0].clone(); x2 = boxes[:, 2].clone()
        boxes[:, 0] = img_size - x2                    # new x1 = W - old x2
        boxes[:, 2] = img_size - x1                    # new x2 = W - old x1
    # Brightness jitter (photometric — boxes unaffected).
    img = (img * rng.uniform(0.7, 1.3)).clamp(0, 1)
    return img, boxes

