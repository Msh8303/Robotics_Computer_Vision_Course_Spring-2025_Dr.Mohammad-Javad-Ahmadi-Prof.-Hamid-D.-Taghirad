"""## 12. Non-Maximum Suppression (NMS) from scratch

A detector predicts a box from *many* anchors, so a single real object typically triggers **several overlapping detections**. We must collapse these duplicates into one box per object. **Non-Maximum Suppression** is the standard greedy algorithm that does this:

1. Sort all detections (of a given class) by confidence, highest first.
2. Take the top box, add it to the "keep" list.
3. Remove every remaining box whose IoU with that top box exceeds a threshold (e.g. 0.5) — they're considered duplicates of it.
4. Repeat with the next-highest remaining box until none remain.

NMS runs **per class** (we don't suppress a "cat" with a "dog"). Let's implement it from scratch, verify it against torchvision's optimized op, then *see* it clean up a cloud of redundant boxes.
"""

def nms(boxes, scores, iou_thresh=0.5):
    """Greedy NMS. boxes:(N,4) xyxy, scores:(N,). Returns kept indices (desc score)."""
    if boxes.numel() == 0:
        return torch.empty(0, dtype=torch.long)
    order = scores.argsort(descending=True)
    keep = []
    while order.numel() > 0:
        i = order[0].item()
        keep.append(i)
        if order.numel() == 1:
            break
        rest = order[1:]
        ious = box_iou(boxes[i][None], boxes[rest])[0]   # (len(rest),)
        order = rest[ious <= iou_thresh]                 # drop overlaps with i
    return torch.tensor(keep, dtype=torch.long)

def batched_nms(boxes, scores, labels, iou_thresh=0.5):
    """Class-aware NMS: suppress only within the same class."""
    keep_all = []
    for c in labels.unique():
        idx = torch.where(labels == c)[0]
        k = nms(boxes[idx], scores[idx], iou_thresh)
        keep_all.append(idx[k])
    return torch.cat(keep_all) if keep_all else torch.empty(0, dtype=torch.long)

