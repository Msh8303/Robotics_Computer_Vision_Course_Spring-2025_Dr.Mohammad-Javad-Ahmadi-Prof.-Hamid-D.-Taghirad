"""## 3. The core vocabulary: bounding boxes & coordinate formats

A **bounding box** is an axis-aligned rectangle that tightly encloses an object. Trivial as it sounds, *box bookkeeping is the #1 source of bugs in detection code*, because there are several common formats and people mix them up constantly. Master these now and you'll save yourself hours later.

The three formats you will meet:

| Format | Representation | Used by |
|---|---|---|
| **`xyxy`** (corners) | `(x_min, y_min, x_max, y_max)` | torchvision, IoU math, drawing |
| **`xywh`** (corner + size) | `(x_min, y_min, w, h)` | COCO annotations |
| **`cxcywh`** (center + size) | `(cx, cy, w, h)` | YOLO, SSD, anchor encoding |

A second axis of confusion is **absolute vs. normalized** coordinates: pixels (e.g. `x=128`) versus fractions of image size (e.g. `x=0.5`). Networks almost always work in **normalized** coordinates so the model is resolution-independent.

Let's implement clean, vectorized conversions and *test them* — a habit worth keeping.
"""

def xywh_to_xyxy(boxes: torch.Tensor) -> torch.Tensor:
    """(x_min, y_min, w, h) -> (x_min, y_min, x_max, y_max)."""
    x, y, w, h = boxes.unbind(-1)
    return torch.stack([x, y, x + w, y + h], dim=-1)

def xyxy_to_xywh(boxes: torch.Tensor) -> torch.Tensor:
    """(x_min, y_min, x_max, y_max) -> (x_min, y_min, w, h)."""
    x1, y1, x2, y2 = boxes.unbind(-1)
    return torch.stack([x1, y1, x2 - x1, y2 - y1], dim=-1)

def cxcywh_to_xyxy(boxes: torch.Tensor) -> torch.Tensor:
    """(cx, cy, w, h) -> (x_min, y_min, x_max, y_max)."""
    cx, cy, w, h = boxes.unbind(-1)
    return torch.stack([cx - w / 2, cy - h / 2, cx + w / 2, cy + h / 2], dim=-1)

def xyxy_to_cxcywh(boxes: torch.Tensor) -> torch.Tensor:
    """(x_min, y_min, x_max, y_max) -> (cx, cy, w, h)."""
    x1, y1, x2, y2 = boxes.unbind(-1)
    return torch.stack([(x1 + x2) / 2, (y1 + y2) / 2, x2 - x1, y2 - y1], dim=-1)

