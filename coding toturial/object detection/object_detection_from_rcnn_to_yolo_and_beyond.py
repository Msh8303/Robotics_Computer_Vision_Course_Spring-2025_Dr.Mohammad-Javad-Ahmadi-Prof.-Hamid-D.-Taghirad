"""## 6. Evaluation metrics: Precision, Recall, AP, mAP

How do we know a detector is *good*? Detection evaluation is more subtle than classification accuracy because of the variable-length output and the need to match predictions to ground truth. Let's build the metric from the ground up.

### Step 1 — Classify each prediction as TP / FP

For one class, sort all predicted boxes by confidence (highest first). Going down the list, match each prediction to ground-truth boxes:

- A prediction is a **True Positive (TP)** if its IoU with an *unmatched* ground-truth box exceeds a threshold (e.g. 0.5). That GT is now "used up."
- Otherwise it's a **False Positive (FP)** — either a duplicate of an already-matched GT, or a box that hits nothing.
- Any ground-truth box never matched is a **False Negative (FN)** — a missed object.

### Step 2 — Precision & Recall

$$\text{Precision} = \frac{TP}{TP + FP} = \frac{\text{correct detections}}{\text{all detections}} \qquad \text{Recall} = \frac{TP}{TP + FN} = \frac{\text{found objects}}{\text{all objects}}$$

**Precision** = "when I say there's an object, how often am I right?" **Recall** = "of all the real objects, how many did I find?" There is a tension: lowering the confidence threshold finds more objects (↑recall) but raises false alarms (↓precision).

### Step 3 — The Precision–Recall curve & Average Precision (AP)

As we sweep the confidence threshold from high to low, we trace out a **PR curve**. **Average Precision (AP)** is the area under this curve — a single number summarizing the precision/recall trade-off for one class. We use the modern **all-points interpolation** (integrate the monotonically-decreasing envelope of the curve), which COCO uses.

### Step 4 — mean Average Precision (mAP)

**mAP** = AP averaged over all classes. The famous **COCO mAP** further averages AP over 10 IoU thresholds (0.50, 0.55, …, 0.95), written **mAP@[.5:.95]**, rewarding tight localization. **mAP@0.5** (the PASCAL VOC metric) is more lenient. We'll implement both.
"""

def compute_ap(recalls, precisions):
    """Area under PR curve, all-points interpolation (COCO/VOC-2010 style)."""
    # Pad with sentinel endpoints so the curve spans recall 0..1.
    mrec  = np.concatenate(([0.0], recalls,  [1.0]))
    mpre  = np.concatenate(([0.0], precisions, [0.0]))
    # Make precision monotonically non-increasing (the "envelope").
    for i in range(len(mpre) - 2, -1, -1):
        mpre[i] = max(mpre[i], mpre[i + 1])
    # Sum areas where recall changes.
    idx = np.where(mrec[1:] != mrec[:-1])[0]
    return float(np.sum((mrec[idx + 1] - mrec[idx]) * mpre[idx + 1]))

def evaluate_detections(all_preds, all_gts, iou_thresh=0.5, num_classes=NUM_CLASSES):
    """Compute per-class AP and mAP at a single IoU threshold.

    all_preds : list (per image) of dict(boxes (P,4), scores (P,), labels (P,))
    all_gts   : list (per image) of dict(boxes (G,4), labels (G,))
    Returns (mAP, {class_id: AP}).
    """
    aps = {}
    for c in range(1, num_classes):                 # skip background
        # Gather every prediction of class c across all images, tagged by image.
        preds = []
        n_gt = 0
        gt_used = []                                 # per-image "matched" flags
        gt_boxes_per_img = []
        for img_idx, (p, g) in enumerate(zip(all_preds, all_gts)):
            gmask = g["labels"] == c
            gboxes = g["boxes"][gmask]
            gt_boxes_per_img.append(gboxes)
            gt_used.append(np.zeros(len(gboxes), dtype=bool))
            n_gt += len(gboxes)
            pmask = p["labels"] == c
            for box, score in zip(p["boxes"][pmask], p["scores"][pmask]):
                preds.append((float(score), img_idx, box))
        if n_gt == 0:
            continue
        preds.sort(key=lambda x: -x[0])              # high confidence first
        tp = np.zeros(len(preds)); fp = np.zeros(len(preds))
        for i, (score, img_idx, box) in enumerate(preds):
            gboxes = gt_boxes_per_img[img_idx]
            if len(gboxes) == 0:
                fp[i] = 1; continue
            ious = box_iou(box[None], gboxes)[0]
            best = torch.argmax(ious).item()
            if ious[best] >= iou_thresh and not gt_used[img_idx][best]:
                tp[i] = 1; gt_used[img_idx][best] = True   # claim this GT
            else:
                fp[i] = 1                                  # dup or bad localization
        tp_cum = np.cumsum(tp); fp_cum = np.cumsum(fp)
        recalls = tp_cum / (n_gt + 1e-9)
        precisions = tp_cum / (tp_cum + fp_cum + 1e-9)
        aps[c] = compute_ap(recalls, precisions)
    mAP = float(np.mean(list(aps.values()))) if aps else 0.0
    return mAP, aps

def coco_map(all_preds, all_gts, num_classes=NUM_CLASSES):
    """mAP averaged over IoU thresholds 0.50:0.05:0.95 (the COCO metric)."""
    thr = np.arange(0.5, 1.0, 0.05)
    return float(np.mean([evaluate_detections(all_preds, all_gts, t, num_classes)[0]
                          for t in thr]))

print("Metric functions ready: compute_ap, evaluate_detections, coco_map.")

