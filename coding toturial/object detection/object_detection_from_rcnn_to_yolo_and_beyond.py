"""## 5. Building our synthetic "Shapes" detection dataset

Real detection datasets (COCO, PASCAL VOC) are gigabytes large and require downloading. To keep this notebook **fully self-contained and fast**, we generate our own dataset of images containing simple colored shapes. This is a genuine, non-trivial detection problem with all the right ingredients:

- **Multiple object classes** — we use 3: `circle`, `rectangle`, `triangle`.
- **A variable number of objects per image** (1 to several).
- **Objects at different positions, scales, and colors.**
- **Occlusion and clutter** (shapes can overlap; we add background noise).
- **Exact ground-truth boxes**, computed analytically as we draw — no manual labeling, zero label noise.

This "shapes world" lets us train real detectors in *minutes* and inspect every prediction, while the *concepts and code* transfer directly to real datasets — you'd only swap the `Dataset` class.

We draw with NumPy for full control over the pixels and the boxes. Each shape is rendered into an `H×W×3` float image in `[0,1]`, and we record a tight `xyxy` box plus the class id.
"""

# Class definitions ------------------------------------------------------
CLASS_NAMES = ["background", "circle", "rectangle", "triangle"]
NUM_CLASSES = len(CLASS_NAMES)          # incl. background at index 0
CIRCLE, RECT, TRI = 1, 2, 3
IMG_SIZE = 128                          # square images, HxW

def _draw_circle(img, cx, cy, r, color):
    H, W = img.shape[:2]
    ys, xs = np.ogrid[:H, :W]
    mask = (xs - cx) ** 2 + (ys - cy) ** 2 <= r ** 2
    img[mask] = color
    return [cx - r, cy - r, cx + r, cy + r]              # xyxy

def _draw_rectangle(img, cx, cy, w, h, color):
    x1, y1 = int(cx - w / 2), int(cy - h / 2)
    x2, y2 = int(cx + w / 2), int(cy + h / 2)
    H, W = img.shape[:2]
    xx1, yy1 = max(0, x1), max(0, y1)
    xx2, yy2 = min(W, x2), min(H, y2)
    img[yy1:yy2, xx1:xx2] = color
    return [x1, y1, x2, y2]

def _draw_triangle(img, cx, cy, size, color):
    """Upward-pointing isoceles triangle, filled via half-plane tests."""
    H, W = img.shape[:2]
    apex   = (cx, cy - size)
    left   = (cx - size, cy + size)
    right  = (cx + size, cy + size)
    ys, xs = np.mgrid[:H, :W]
    def side(p1, p2):                 # signed side of line p1->p2
        return (p2[0]-p1[0])*(ys-p1[1]) - (p2[1]-p1[1])*(xs-p1[0])
    d1, d2, d3 = side(apex, left), side(left, right), side(right, apex)
    has_neg = (d1 < 0) | (d2 < 0) | (d3 < 0)
    has_pos = (d1 > 0) | (d2 > 0) | (d3 > 0)
    mask = ~(has_neg & has_pos)        # inside if all same sign
    img[mask] = color
    return [cx - size, cy - size, cx + size, cy + size]

def generate_image(max_objects=4, img_size=IMG_SIZE, add_noise=True, rng=None):
    """Return (image HxWx3 float, boxes (N,4) xyxy, labels (N,))."""
    rng = rng or np.random
    # light, slightly textured background so it isn't trivially separable
    img = np.ones((img_size, img_size, 3), dtype=np.float32) * rng.uniform(0.85, 1.0)
    boxes, labels = [], []
    n = rng.randint(1, max_objects + 1)
    for _ in range(n):
        cls = rng.choice([CIRCLE, RECT, TRI])
        color = rng.uniform(0.0, 0.7, size=3).astype(np.float32)  # avoid near-white
        margin = 18
        cx = rng.randint(margin, img_size - margin)
        cy = rng.randint(margin, img_size - margin)
        if cls == CIRCLE:
            r = rng.randint(8, 20)
            box = _draw_circle(img, cx, cy, r, color)
        elif cls == RECT:
            w = rng.randint(16, 38); h = rng.randint(16, 38)
            box = _draw_rectangle(img, cx, cy, w, h, color)
        else:
            s = rng.randint(10, 22)
            box = _draw_triangle(img, cx, cy, s, color)
        # clip box to image bounds and keep only if it has real area
        x1, y1, x2, y2 = box
        x1 = max(0, min(x1, img_size - 1)); y1 = max(0, min(y1, img_size - 1))
        x2 = max(0, min(x2, img_size));     y2 = max(0, min(y2, img_size))
        if x2 - x1 >= 4 and y2 - y1 >= 4:
            boxes.append([x1, y1, x2, y2]); labels.append(cls)
    if add_noise:
        img += rng.normal(0, 0.02, img.shape).astype(np.float32)
        img = np.clip(img, 0, 1)
    if not boxes:                       # guarantee >=1 object
        return generate_image(max_objects, img_size, add_noise, rng)
    return img, np.array(boxes, dtype=np.float32), np.array(labels, dtype=np.int64)

