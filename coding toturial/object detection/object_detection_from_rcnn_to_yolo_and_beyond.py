"""### Encoding & decoding box deltas

The network does **not** output box coordinates directly. It outputs four numbers $(t_x, t_y, t_w, t_h)$ per anchor — the **deltas**. The standard parameterization (from R-CNN, used by Faster R-CNN, SSD, YOLO-style detectors) is:

$$t_x = \frac{g_x - a_x}{a_w},\quad t_y = \frac{g_y - a_y}{a_h},\quad t_w = \log\frac{g_w}{a_w},\quad t_h = \log\frac{g_h}{a_h}$$

where $a$ = anchor (center form), $g$ = ground-truth box. **Encoding** (above) turns a GT box into the target the network should predict, *relative to its anchor*. **Decoding** inverts it to turn predictions back into boxes:

$$g_x = a_x + t_x a_w,\quad g_w = a_w \, e^{t_w},\quad \dots$$

Why this form? The center offsets are normalized by anchor size (scale-invariant), and the size uses **log-space** so that "doubling" and "halving" are symmetric and the regression target is well-behaved (a log ratio is unbounded and roughly Gaussian, ideal for L1/L2 losses). Let's implement and round-trip-test it.
"""

def encode_boxes(gt_boxes, anchors):
    """xyxy gt & anchors -> (tx,ty,tw,th) deltas. Both (N,4)."""
    g = xyxy_to_cxcywh(gt_boxes); a = xyxy_to_cxcywh(anchors)
    tx = (g[:, 0] - a[:, 0]) / a[:, 2]
    ty = (g[:, 1] - a[:, 1]) / a[:, 3]
    tw = torch.log(g[:, 2] / a[:, 2])
    th = torch.log(g[:, 3] / a[:, 3])
    return torch.stack([tx, ty, tw, th], dim=1)

def decode_boxes(deltas, anchors):
    """(tx,ty,tw,th) deltas + anchors -> xyxy boxes. Both (N,4)."""
    a = xyxy_to_cxcywh(anchors)
    cx = deltas[:, 0] * a[:, 2] + a[:, 0]
    cy = deltas[:, 1] * a[:, 3] + a[:, 1]
    w  = torch.exp(deltas[:, 2].clamp(max=4.0)) * a[:, 2]   # clamp guards exp overflow
    h  = torch.exp(deltas[:, 3].clamp(max=4.0)) * a[:, 3]
    return cxcywh_to_xyxy(torch.stack([cx, cy, w, h], dim=1))

# Round-trip test: encode a GT relative to an anchor, decode, recover the GT.
_anchor = torch.tensor([[40., 40., 80., 80.]])
_gt     = torch.tensor([[45., 50., 95., 88.]])
_delta  = encode_boxes(_gt, _anchor)
