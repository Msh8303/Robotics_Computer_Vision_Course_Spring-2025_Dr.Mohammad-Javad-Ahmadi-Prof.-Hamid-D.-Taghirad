"""## 6. Train every model end-to-end

One unified loop fine-tunes each architecture on the **same** training data with the **same** budget — the only fair way to compare. We use Adam with a cosine schedule and gradient clipping (detection losses can spike early). For each model we record the per-epoch loss and wall-clock time, then cache its validation predictions once for all downstream analysis.

> **Time:** on a Colab **T4 GPU**, the MobileNet models train in well under a minute per epoch; the ResNet50 models a bit more. The full default run is a few minutes per model. Use `QUICK_MODE` (top of notebook) for a fast smoke test. Results are stored in `RESULTS`, so you can re-run any analysis cell without retraining.
"""

def train_model(name, epochs=None, lr=None):
    """Fine-tune one model; return (model, history) with per-epoch loss/time."""
    epochs = epochs or CONFIG.epochs
    lr = lr or CONFIG.lr
    model = MODEL_BUILDERS[name]().to(DEVICE)
    loader = DataLoader(train_set, batch_size=CONFIG.batch_size, shuffle=True,
                        collate_fn=collate, num_workers=2, drop_last=True)
    opt = torch.optim.Adam([p for p in model.parameters() if p.requires_grad],
                           lr=lr, weight_decay=1e-4)
    sched = torch.optim.lr_scheduler.CosineAnnealingLR(opt, T_max=epochs)
    hist = {"loss": [], "time": []}
    for ep in range(epochs):
        model.train(); t0 = time.time(); tot = 0.0; nb = 0
        for images, targets in loader:
            images = [im.to(DEVICE) for im in images]
            targets = [{k: v.to(DEVICE) for k, v in t.items()} for t in targets]
            loss_dict = model(images, targets)
            loss = sum(loss_dict.values())
            opt.zero_grad(); loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 5.0)
            opt.step(); tot += float(loss); nb += 1
        sched.step()
        hist["loss"].append(tot / nb); hist["time"].append(time.time() - t0)
        print(f"  [{MODEL_PRETTY[name]:18s}] epoch {ep+1:2d}/{epochs} "
              f"loss {hist['loss'][-1]:6.3f} ({hist['time'][-1]:4.1f}s)")
    return model, hist

