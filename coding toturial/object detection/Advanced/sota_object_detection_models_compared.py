"""## 10. Operating-point & robustness analyses

A trained detector isn't deployed as-is — you choose its **operating point**: the confidence threshold (how sure before reporting) and the NMS IoU threshold (how aggressively to merge). These choices trade precision against recall and aren't captured by mAP (which integrates over all thresholds). Here we make the trade-offs explicit.

### 10.1 Confidence-threshold sweep → precision / recall / F1

As you raise the score threshold, precision rises (fewer false alarms) but recall falls (more misses). The **F1-optimal threshold** is a common deployment choice. We sweep it for each model on one class.
"""

def pr_at_threshold(preds, gts, cls, score_thr, iou_thr=0.5):
    tp = fp = 0; n_gt = 0
    for p, g in zip(preds, gts):
        gm = g["labels"] == cls; gb = g["boxes"][gm]; n_gt += len(gb)
        used = np.zeros(len(gb), bool)
        pm = (p["labels"] == cls) & (p["scores"] >= score_thr)
        pb = p["boxes"][pm]; psc = p["scores"][pm]
        for box in pb[np.argsort(-psc)]:
            if len(gb) == 0: fp += 1; continue
            ious = box_iou(torch.tensor(box[None]), torch.tensor(gb))[0].numpy()
            j = int(ious.argmax())
            if ious[j] >= iou_thr and not used[j]: tp += 1; used[j] = True
            else: fp += 1
    prec = tp/(tp+fp+1e-9); rec = tp/(n_gt+1e-9)
    f1 = 2*prec*rec/(prec+rec+1e-9)
    return prec, rec, f1

