"""### 10.3 Confidence calibration (reliability diagram)

When a model says *"0.9 confident,"* is it right 90% of the time? A **well-calibrated** detector's confidence matches its actual precision. We bin detections by confidence and plot mean confidence vs. empirical precision; the diagonal is perfect calibration. Detectors are often *over*-confident — important to know if you set thresholds or fuse scores downstream.
"""

def calibration(preds, gts, iou_thr=0.5, n_bins=10):
    """Return (bin_centers, empirical_precision, mean_conf, counts)."""
    confs, correct = [], []
    for p, g in zip(preds, gts):
        order = np.argsort(-p["scores"])
        pb, pl, ps = p["boxes"][order], p["labels"][order], p["scores"][order]
        used = defaultdict(lambda: np.zeros(len(g["boxes"]), bool))
        for box, lab, sc in zip(pb, pl, ps):
            gm = np.where(g["labels"] == lab)[0]
            ok = 0
            if len(gm):
                ious = box_iou(torch.tensor(box[None]),
                               torch.tensor(g["boxes"][gm]))[0].numpy()
                j = int(ious.argmax())
                if ious[j] >= iou_thr and not used[lab][gm[j]]:
                    ok = 1; used[lab][gm[j]] = True
            confs.append(sc); correct.append(ok)
    confs = np.array(confs); correct = np.array(correct)
    edges = np.linspace(0, 1, n_bins+1)
    centers, emp, mc, cnt = [], [], [], []
    for i in range(n_bins):
        m = (confs >= edges[i]) & (confs < edges[i+1])
        if m.sum() == 0: continue
        centers.append((edges[i]+edges[i+1])/2)
        emp.append(correct[m].mean()); mc.append(confs[m].mean()); cnt.append(int(m.sum()))
    return np.array(centers), np.array(emp), np.array(mc), np.array(cnt)

