"""## 2. A realistic synthetic detection dataset

To exercise the detectors properly we generate scenes that contain the difficulties of real detection:

- **4 object classes** — `circle`, `rectangle`, `triangle`, `ellipse` — for genuine multi-class confusion analysis.
- **A wide range of object sizes**, deliberately including **small objects** (the hardest case) so we can measure AP by size.
- **Textured, non-uniform backgrounds** and sensor-like noise, so objects aren't trivially separable.
- **Occlusion / clutter** — objects overlap, and labels remain the full analytic extent (as in real annotation).
- **Variable object counts** per image (1–8).

Ground-truth boxes are computed analytically as we render, so labels are exact. We also record each object's **size category** (small / medium / large) for the size-stratified evaluation later.
"""

CLASS_NAMES = ["background", "circle", "rectangle", "triangle", "ellipse"]
NUM_CLASSES = len(CLASS_NAMES)          # 5 (incl. background at 0)
CIRCLE, RECT, TRI, ELL = 1, 2, 3, 4

# Size bands (by box area, in pixels) for AP_small / AP_medium / AP_large.
SIZE_SMALL_MAX  = 26 ** 2
SIZE_MEDIUM_MAX = 56 ** 2

def _bg(img_size, rng):
    """Low-frequency textured background + slight vignette — not flat."""
    small = rng.uniform(0.55, 0.95, size=(8, 8, 3)).astype(np.float32)
    bg = np.array(_resize_np(small, img_size), dtype=np.float32)
    yy, xx = np.mgrid[0:img_size, 0:img_size]
    vign = 1.0 - 0.25 * (((xx - img_size/2)**2 + (yy - img_size/2)**2) /
                         (img_size/2)**2)
    return np.clip(bg * vign[..., None], 0, 1).astype(np.float32)

def _resize_np(a, size):
    """Bilinear upsample a small HxWxC array to size x size (no extra deps)."""
    t = torch.from_numpy(a).permute(2, 0, 1)[None]
    t = F.interpolate(t, size=(size, size), mode="bilinear", align_corners=False)
    return t[0].permute(1, 2, 0).numpy()

def _circle(img, cx, cy, r, color):
    H, W = img.shape[:2]; ys, xs = np.ogrid[:H, :W]
    img[(xs-cx)**2 + (ys-cy)**2 <= r*r] = color
    return [cx-r, cy-r, cx+r, cy+r]

def _ellipse(img, cx, cy, rx, ry, color):
    H, W = img.shape[:2]; ys, xs = np.ogrid[:H, :W]
    img[((xs-cx)/rx)**2 + ((ys-cy)/ry)**2 <= 1.0] = color
    return [cx-rx, cy-ry, cx+rx, cy+ry]

def _rect(img, cx, cy, w, h, color):
    H, W = img.shape[:2]
    x1, y1 = int(cx-w/2), int(cy-h/2); x2, y2 = int(cx+w/2), int(cy+h/2)
    img[max(0,y1):min(H,y2), max(0,x1):min(W,x2)] = color
    return [x1, y1, x2, y2]

def _triangle(img, cx, cy, s, color):
    H, W = img.shape[:2]
    apex, left, right = (cx, cy-s), (cx-s, cy+s), (cx+s, cy+s)
    ys, xs = np.mgrid[:H, :W]
    def side(p, q): return (q[0]-p[0])*(ys-p[1]) - (q[1]-p[1])*(xs-p[0])
    d1, d2, d3 = side(apex,left), side(left,right), side(right,apex)
    neg = (d1<0)|(d2<0)|(d3<0); pos = (d1>0)|(d2>0)|(d3>0)
    img[~(neg & pos)] = color
    return [cx-s, cy-s, cx+s, cy+s]

def size_category(box):
    area = max(0, (box[2]-box[0])) * max(0, (box[3]-box[1]))
    if area <= SIZE_SMALL_MAX:  return "small"
    if area <= SIZE_MEDIUM_MAX: return "medium"
    return "large"

def generate_scene(img_size=256, max_objects=8, rng=None):
    rng = rng or np.random
    img = _bg(img_size, rng)
    boxes, labels = [], []
    n = rng.randint(1, max_objects + 1)
    for _ in range(n):
        cls = rng.choice([CIRCLE, RECT, TRI, ELL])
        color = rng.uniform(0.0, 0.6, size=3).astype(np.float32)
        # Bias toward more small/medium objects (realistic + stresses detectors).
        scale_band = rng.choice(["small", "medium", "large"], p=[0.4, 0.4, 0.2])
        if scale_band == "small":   s = rng.randint(6, 13)
        elif scale_band == "medium": s = rng.randint(14, 28)
        else:                        s = rng.randint(30, 48)
        m = s + 2
        cx, cy = rng.randint(m, img_size-m), rng.randint(m, img_size-m)
        if cls == CIRCLE:   box = _circle(img, cx, cy, s, color)
        elif cls == RECT:   box = _rect(img, cx, cy, int(s*rng.uniform(1.0,2.0)),
                                        int(s*rng.uniform(1.0,2.0)), color)
        elif cls == TRI:    box = _triangle(img, cx, cy, s, color)
        else:               box = _ellipse(img, cx, cy, int(s*rng.uniform(1.0,1.8)),
                                           s, color)
        x1 = max(0, min(box[0], img_size-1)); y1 = max(0, min(box[1], img_size-1))
        x2 = max(0, min(box[2], img_size));   y2 = max(0, min(box[3], img_size))
        if x2-x1 >= 4 and y2-y1 >= 4:
            boxes.append([x1, y1, x2, y2]); labels.append(cls)
    img = np.clip(img + rng.normal(0, 0.025, img.shape).astype(np.float32), 0, 1)
    img = img.astype(np.float32)
    if not boxes:
        return generate_scene(img_size, max_objects, rng)
    return img, np.array(boxes, np.float32), np.array(labels, np.int64)

class DetectionDataset(Dataset):
    """Self-contained synthetic detection dataset (torchvision-compatible)."""
    def __init__(self, length, img_size=256, seed=0):
        self.length, self.img_size, self.seed = length, img_size, seed
    def __len__(self): return self.length
    def __getitem__(self, idx):
        rng = np.random.RandomState(self.seed * 1_000_003 + idx)
        img, boxes, labels = generate_scene(self.img_size, rng=rng)
        img = torch.from_numpy(img).permute(2, 0, 1).contiguous()
        target = {"boxes": torch.from_numpy(boxes),
                  "labels": torch.from_numpy(labels)}
        return img, target

