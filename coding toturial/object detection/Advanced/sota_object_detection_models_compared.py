"""## 5. The evaluation toolkit

A detector is only as trustworthy as its evaluation. We build a reusable toolkit — the same machinery (in spirit) as the official COCO evaluator — so every model is judged identically and fairly.

Core pieces:

- **`box_iou`** — vectorized pairwise IoU.
- **`average_precision`** — AP from a ranked list of detections via all-points interpolation, with optional **size filtering** and **ignore** logic (so we can compute AP_small / AP_medium / AP_large the COCO way: objects of other sizes are *ignored*, not counted as errors).
- **`evaluate_model`** — runs a model over the validation set **once**, caching all raw predictions (at a low score threshold). Every subsequent metric and plot reads from this cache, so the expensive forward passes happen only once per model.
- **`compute_all_metrics`** — turns cached predictions into mAP@.5, mAP@[.5:.95], per-class AP, and per-size AP.

We reuse `torchvision.ops.nms`/`box_iou` where the model already applied NMS internally; our job downstream is purely measurement.
"""

def box_iou(a, b):
    """Pairwise IoU. a:(N,4) b:(M,4) xyxy -> (N,M)."""
    if len(a) == 0 or len(b) == 0:
        return torch.zeros((len(a), len(b)))
    area_a = (a[:,2]-a[:,0]).clamp(min=0)*(a[:,3]-a[:,1]).clamp(min=0)
    area_b = (b[:,2]-b[:,0]).clamp(min=0)*(b[:,3]-b[:,1]).clamp(min=0)
    lt = torch.max(a[:,None,:2], b[None,:,:2])
    rb = torch.min(a[:,None,2:], b[None,:,2:])
    wh = (rb-lt).clamp(min=0); inter = wh[...,0]*wh[...,1]
    return inter / (area_a[:,None]+area_b[None,:]-inter).clamp(min=1e-9)

def _ap_from_pr(rec, prec):
    """All-points (COCO/VOC-2010) interpolation: area under PR envelope."""
    mrec = np.concatenate(([0.0], rec, [1.0]))
    mpre = np.concatenate(([0.0], prec, [0.0]))
    for i in range(len(mpre)-2, -1, -1):
        mpre[i] = max(mpre[i], mpre[i+1])
    idx = np.where(mrec[1:] != mrec[:-1])[0]
    return float(np.sum((mrec[idx+1]-mrec[idx])*mpre[idx+1]))

def average_precision(preds, gts, cls, iou_thr=0.5, area_range=None,
                      return_curve=False):
    """AP for one class at one IoU threshold, COCO-style size handling.

    preds/gts : per-image lists. pred dict has boxes,scores,labels (np);
                gt dict has boxes,labels (np).
    area_range: optional (min_area, max_area); GTs outside it are *ignored*
                (matched detections become neither TP nor FP) -> AP_S/M/L.
    """
    def in_range(box):
        if area_range is None: return True
        a = (box[2]-box[0])*(box[3]-box[1])
        return area_range[0] <= a < area_range[1]

    scored = []   # (score, img_idx, box)
    n_gt = 0
    gt_boxes, gt_keep, gt_used = [], [], []
    for i,(p,g) in enumerate(zip(preds, gts)):
        gm = g["labels"] == cls
        gb = g["boxes"][gm]
        keep = np.array([in_range(b) for b in gb], dtype=bool) if len(gb) \
               else np.zeros(0, bool)
        gt_boxes.append(gb); gt_keep.append(keep)
        gt_used.append(np.zeros(len(gb), bool))
        n_gt += int(keep.sum())
        pm = p["labels"] == cls
        for box, sc in zip(p["boxes"][pm], p["scores"][pm]):
            scored.append((float(sc), i, box))
    if n_gt == 0:
        return (0.0, np.array([0.]), np.array([0.])) if return_curve else 0.0
    scored.sort(key=lambda x: -x[0])
    tp = np.zeros(len(scored)); fp = np.zeros(len(scored))
    for k,(sc,i,box) in enumerate(scored):
        gb = gt_boxes[i]
        if len(gb) == 0: fp[k] = 1; continue
        ious = box_iou(torch.tensor(box[None]), torch.tensor(gb))[0].numpy()
        j = int(ious.argmax())
        if ious[j] >= iou_thr:
            if not gt_keep[i][j]:
                # matched an out-of-size-range GT -> ignore (neither TP nor FP)
                if not gt_used[i][j]: gt_used[i][j] = True
                continue
            if not gt_used[i][j]:
                tp[k] = 1; gt_used[i][j] = True
            else:
                fp[k] = 1                      # duplicate
        else:
            fp[k] = 1                          # poor localization / background
    tpc, fpc = np.cumsum(tp), np.cumsum(fp)
    rec = tpc / (n_gt + 1e-9)
    prec = tpc / (tpc + fpc + 1e-9)
    ap = _ap_from_pr(rec, prec)
    return (ap, rec, prec) if return_curve else ap

print("Metric primitives ready: box_iou, average_precision (+size/curve support).")

@torch.no_grad()
def collect_predictions(model, dataset, batch_size=8, score_thresh=0.05):
    """Run the model over a dataset ONCE; cache predictions + ground truth.
    Returns (preds, gts) as per-image lists of numpy dicts."""
    model.eval()
    loader = DataLoader(dataset, batch_size=batch_size, collate_fn=collate,
                        num_workers=2)
    preds, gts = [], []
    for images, targets in loader:
        images = [im.to(DEVICE) for im in images]
        outputs = model(images)
        for out, tgt in zip(outputs, targets):
            keep = out["scores"] >= score_thresh
            preds.append({"boxes": out["boxes"][keep].cpu().numpy(),
                          "scores": out["scores"][keep].cpu().numpy(),
                          "labels": out["labels"][keep].cpu().numpy()})
            gts.append({"boxes": tgt["boxes"].numpy(),
                        "labels": tgt["labels"].numpy()})
    return preds, gts

def compute_all_metrics(preds, gts):
    """COCO-style summary from cached predictions."""
    classes = list(range(1, NUM_CLASSES))
    iou_thrs = np.arange(0.5, 1.0, 0.05)
    # per-class AP@.5 and AP@[.5:.95]
    ap50 = {c: average_precision(preds, gts, c, 0.5) for c in classes}
    ap_coco = {c: float(np.mean([average_precision(preds, gts, c, t)
                                 for t in iou_thrs])) for c in classes}
    # per-size AP@.5 (averaged over classes)
    ranges = {"small": (0, SIZE_SMALL_MAX),
              "medium": (SIZE_SMALL_MAX, SIZE_MEDIUM_MAX),
              "large": (SIZE_MEDIUM_MAX, 1e9)}
    ap_size = {}
    for sname, rng in ranges.items():
        vals = [average_precision(preds, gts, c, 0.5, area_range=rng)
                for c in classes]
        ap_size[sname] = float(np.mean(vals))
    return {
        "mAP@.5": float(np.mean(list(ap50.values()))),
        "mAP@[.5:.95]": float(np.mean(list(ap_coco.values()))),
        "AP50_per_class": {CLASS_NAMES[c]: ap50[c] for c in classes},
        "AP_small": ap_size["small"], "AP_medium": ap_size["medium"],
        "AP_large": ap_size["large"],
    }

print("collect_predictions() and compute_all_metrics() ready.")

"""### Speed benchmark & error-analysis tools

Two more tools complete the kit:

- **`benchmark_latency`** — measures single-image inference time (with warmup, and CUDA synchronization when on GPU) → milliseconds/image and FPS. Speed is half of the real-world decision.
- **`categorize_errors`** — a **TIDE-style** decomposition of *what kind* of mistakes a model makes, at a fixed operating threshold. Every prediction is sorted into one of: **correct**, **duplicate**, **localization error** (right class, loose box), **classification error** (right place, wrong class), or **background error** (hallucination). Separately we count **missed** ground-truth objects (false negatives). This tells you *how to fix* a model, not just *that* it's imperfect — far more actionable than a single mAP number.
"""

@torch.no_grad()
def benchmark_latency(model, n_warmup=5, n_runs=40, img_size=None):
    """Return (ms_per_image, fps) for batch-1 inference."""
    model.eval()
    img_size = img_size or CONFIG.img_size
    x = [torch.rand(3, img_size, img_size, device=DEVICE)]
    for _ in range(n_warmup):
        model(x)
    if DEVICE.type == "cuda": torch.cuda.synchronize()
    t0 = time.time()
    for _ in range(n_runs):
        model(x)
    if DEVICE.type == "cuda": torch.cuda.synchronize()
    ms = (time.time() - t0) / n_runs * 1000
    return ms, 1000.0 / ms

def categorize_errors(preds, gts, score_thresh=0.3, iou_correct=0.5, iou_loc=0.1):
    """TIDE-lite error decomposition over the dataset at a fixed threshold.
    Returns dict of counts: correct, duplicate, localization, classification,
    background, missed."""
    cats = defaultdict(int)
    for p, g in zip(preds, gts):
        order = np.argsort(-p["scores"])
        pb, pl, ps = p["boxes"][order], p["labels"][order], p["scores"][order]
        sel = ps >= score_thresh
        pb, pl = pb[sel], pl[sel]
        gb, gl = g["boxes"], g["labels"]
        used = np.zeros(len(gb), bool)
        if len(gb):
            ious_all = box_iou(torch.tensor(pb), torch.tensor(gb)).numpy() \
                       if len(pb) else np.zeros((0, len(gb)))
        for k in range(len(pb)):
            if len(gb) == 0:
                cats["background"] += 1; continue
            ious = ious_all[k]
            j = int(ious.argmax()); best = ious[j]
            if best >= iou_correct:
                if gl[j] == pl[k]:
                    if not used[j]: cats["correct"] += 1; used[j] = True
                    else:           cats["duplicate"] += 1
                else:
                    cats["classification"] += 1     # right box, wrong class
            elif best >= iou_loc:
                # same class but loose box -> localization; else still background-ish
                cats["localization"] += 1 if gl[j] == pl[k] else 1
            else:
                cats["background"] += 1
        cats["missed"] += int((~used).sum())
    return dict(cats)

def confusion_matrix(preds, gts, score_thresh=0.3, iou_thr=0.5):
    """(NUM_CLASSES) x (NUM_CLASSES) matrix incl. background row/col.
    Row = true class (0=missed/background), Col = predicted (0=none)."""
    M = np.zeros((NUM_CLASSES, NUM_CLASSES), dtype=int)
    for p, g in zip(preds, gts):
        order = np.argsort(-p["scores"])
        pb, pl, ps = p["boxes"][order], p["labels"][order], p["scores"][order]
        sel = ps >= score_thresh; pb, pl = pb[sel], pl[sel]
        gb, gl = g["boxes"], g["labels"]
        used = np.zeros(len(gb), bool)
        ious_all = box_iou(torch.tensor(pb), torch.tensor(gb)).numpy() \
                   if (len(pb) and len(gb)) else np.zeros((len(pb), len(gb)))
        for k in range(len(pb)):
            if len(gb) == 0: M[0, pl[k]] += 1; continue
            j = int(ious_all[k].argmax())
            if ious_all[k][j] >= iou_thr and not used[j]:
                M[gl[j], pl[k]] += 1; used[j] = True
            else:
                M[0, pl[k]] += 1                 # predicted but no true match
        for j in range(len(gb)):
            if not used[j]: M[gl[j], 0] += 1     # true object missed
    return M

