modes = {
    'CONSTANT'   : cv2.BORDER_CONSTANT,
    'REPLICATE'  : cv2.BORDER_REPLICATE,
    'REFLECT'    : cv2.BORDER_REFLECT,
    'REFLECT_101': cv2.BORDER_REFLECT_101,
    'WRAP'       : cv2.BORDER_WRAP,
}

outs = []
for name, m in modes.items():
    padded = cv2.copyMakeBorder(img, 50, 50, 50, 50, m, value=(0, 255, 0))
    outs.append(padded)

show_grid(outs, list(modes.keys()), cols=3, figsize=(15, 8))

"""## 23.3 `connectedComponentsWithStats` — count & label blobs in one call

Stronger than `findContours` when you also want areas, bounding boxes, and centroids.

"""

coins = cv2.imread('images/coins.jpg', cv2.IMREAD_GRAYSCALE)
_, bw = cv2.threshold(coins, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
bw = cv2.morphologyEx(bw, cv2.MORPH_OPEN, np.ones((3, 3), np.uint8), iterations=2)

n_labels, labels, stats, centroids = cv2.connectedComponentsWithStats(bw, connectivity=8)
print(f"Labels (incl. background): {n_labels}")
print(f"Stats shape: {stats.shape}  (label, [x, y, w, h, area])")
print(f"Centroids shape: {centroids.shape}")

# Print info for each non-background label
print("\nLabel | x   y    w   h   area  centroid")
print("------|----------------------------------")
for i in range(1, n_labels):
    x, y, w, h, area = stats[i]
    cx, cy = centroids[i]
    print(f"{i:5d} | {x:3d} {y:3d}  {w:3d} {h:3d}  {area:4d}  ({cx:5.1f},{cy:5.1f})")

# Pretty visualization
rng = np.random.default_rng(0)
colors = rng.integers(50, 255, size=(n_labels, 3), dtype=np.uint8)
colors[0] = 0  # background black
vis = colors[labels]
show(vis, title=f"connectedComponentsWithStats — {n_labels - 1} blobs")

