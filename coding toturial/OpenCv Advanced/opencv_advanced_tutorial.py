"""---
# 2. 🏔️ Image Pyramids — Multi-Scale Processing

A **pyramid** is a stack of progressively smaller (or larger) versions of the same image. They're a foundational tool for:

- **Coarse-to-fine search**: detect at low res, refine at high res — much faster
- **Multi-scale feature detection**: SIFT internally builds a Gaussian pyramid
- **Image blending**: Laplacian pyramids let you seamlessly blend two images

OpenCV provides:
- `cv2.pyrDown(img)` — Gaussian-filter then downsample by 2 → halves both dimensions
- `cv2.pyrUp(img)` — upsample by 2 then Gaussian-filter → doubles both dimensions

> ⚠️ `pyrUp(pyrDown(img))` does **not** equal `img` — information is lost. The difference is what makes Laplacian pyramids work.

"""

img = cv2.imread('images/astronaut.jpg')
print(f"Original: {img.shape}")

# Build a 4-level Gaussian pyramid
gp = [img]
for i in range(4):
    gp.append(cv2.pyrDown(gp[-1]))
    print(f"Level {i+1}: {gp[-1].shape}  (1/{2**(i+1)} resolution)")

show_grid(gp, [f"L{i}: {im.shape[1]}x{im.shape[0]}" for i, im in enumerate(gp)],
          cols=5, figsize=(15, 3))

"""## 2.1 Laplacian pyramid

A Laplacian pyramid stores the *details lost* at each level of the Gaussian pyramid. Reconstruct by adding the details back as you upsample. Used for **seamless image blending** — paste apple half + orange half = "orapple" with no visible seam.

"""

# Laplacian level i = G_i  -  pyrUp(G_{i+1})
# We need to size-match because pyrUp doesn't always give exactly 2x.
lp = []
for i in range(len(gp) - 1):
    upsampled = cv2.pyrUp(gp[i + 1], dstsize=(gp[i].shape[1], gp[i].shape[0]))
    laplacian = cv2.subtract(gp[i], upsampled)
    lp.append(laplacian)
    print(f"Laplacian L{i}: shape={laplacian.shape}, "
          f"range=[{laplacian.min()}, {laplacian.max()}], mean={laplacian.mean():.1f}")

# Visualize — add 128 so negative differences are visible
viz = [cv2.add(l, 128) for l in lp]
show_grid(viz, [f"Laplacian L{i}" for i in range(len(lp))], cols=4, figsize=(14, 4))

# Demonstrate reconstruction (last Gaussian + Laplacians sum back to the original)
recon = gp[-1]
for i in reversed(range(len(lp))):
    recon = cv2.pyrUp(recon, dstsize=(lp[i].shape[1], lp[i].shape[0]))
    recon = cv2.add(recon, lp[i])
err = np.abs(recon.astype(int) - img.astype(int)).mean()
print(f"\nReconstruction error vs. original: {err:.3f} per-pixel "
      f"(should be near zero)")

"""---
# 3. 📐 Hough Transforms — Detecting Lines and Circles

The Hough transform finds parametric shapes (lines, circles) by **voting**: each edge pixel "votes" for every shape that could pass through it; peaks in the parameter space are the detected shapes.

**Robotics applications**: lane detection in self-driving, pipe/wire detection in industrial robotics, finding round fiducials.

## 3.1 Probabilistic Hough lines

`cv2.HoughLinesP` is the **practical** version — it returns concrete line segments `(x1, y1, x2, y2)` instead of infinite lines.
"""

# Build a scene with strong straight edges — checkerboard works perfectly
checker = cv2.imread('images/checkerboard.png', cv2.IMREAD_GRAYSCALE)

edges = cv2.Canny(checker, 50, 150)

# threshold = minimum vote count for a line to be returned
# minLineLength = shortest line we accept
# maxLineGap = max pixel gap allowed within one line
lines = cv2.HoughLinesP(edges, rho=1, theta=np.pi/180,
                        threshold=80, minLineLength=30, maxLineGap=10)

print(f"Lines detected: {0 if lines is None else len(lines)}")
print(f"Output array shape: {None if lines is None else lines.shape}  "
      f"(N lines, 1, 4 endpoints)")

vis = cv2.cvtColor(checker, cv2.COLOR_GRAY2BGR)
if lines is not None:
    for x1, y1, x2, y2 in lines.squeeze():
        cv2.line(vis, (x1, y1), (x2, y2), (0, 0, 255), 2)

show_grid([checker, edges, vis],
          ["Input", "Canny edges", f"HoughLinesP — {len(lines)} segments"],
          cols=3, figsize=(13, 4))

"""## 3.2 Hough circles

`cv2.HoughCircles` finds circles. The classic demo: counting coins.

"""

coins = cv2.imread('images/coins.jpg', cv2.IMREAD_GRAYSCALE)
coins_blur = cv2.medianBlur(coins, 5)

# Parameters that matter:
#   dp        : inverse accumulator resolution (1 = same as input)
#   minDist   : minimum distance between detected centers
#   param1    : higher Canny threshold (lower = param1/2)
#   param2    : accumulator threshold (smaller = more circles, more false positives)
#   minRadius / maxRadius : expected circle size range
circles = cv2.HoughCircles(coins_blur, cv2.HOUGH_GRADIENT, dp=1.2, minDist=30,
                            param1=80, param2=30, minRadius=15, maxRadius=40)

vis = cv2.cvtColor(coins, cv2.COLOR_GRAY2BGR)
if circles is not None:
    circles = np.round(circles[0]).astype(int)
    print(f"Circles detected: {len(circles)}")
    for i, (x, y, r) in enumerate(circles):
        cv2.circle(vis, (x, y), r, (0, 255, 0), 2)
        cv2.circle(vis, (x, y), 2, (0, 0, 255), 3)
        cv2.putText(vis, str(i + 1), (x - 10, y - r - 5),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.5, (0, 0, 255), 1)

show_grid([coins, coins_blur, vis],
          ["Input", "Median-blurred", f"HoughCircles — {len(circles)} coins"],
          cols=3, figsize=(13, 4))

"""---
# 4. 💧 Watershed Segmentation — Separating Touching Objects

When objects **touch**, simple thresholding merges them into one blob. The **watershed algorithm** treats the image like a topographic surface and "floods" it from marked seed points — boundaries form where floods meet.

The trick: provide good **markers**. We use the distance transform to find object centers and morphology to find the sure-background.

"""

coins = cv2.imread('images/coins.jpg')
gray  = cv2.cvtColor(coins, cv2.COLOR_BGR2GRAY)

# Step 1: clean binary mask
_, thresh = cv2.threshold(gray, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
thresh = cv2.morphologyEx(thresh, cv2.MORPH_OPEN, np.ones((3, 3), np.uint8), iterations=2)

# Step 2: sure background = dilation of foreground
sure_bg = cv2.dilate(thresh, np.ones((3, 3), np.uint8), iterations=3)

# Step 3: sure foreground = peaks of the distance transform
dist = cv2.distanceTransform(thresh, cv2.DIST_L2, 5)
print(f"Distance transform: shape={dist.shape}, dtype={dist.dtype}, "
      f"range=[{dist.min():.1f}, {dist.max():.1f}]")
_, sure_fg = cv2.threshold(dist, 0.5 * dist.max(), 255, 0)
sure_fg = sure_fg.astype(np.uint8)

# Step 4: unknown = bg - fg
unknown = cv2.subtract(sure_bg, sure_fg)

# Step 5: connected-component labeling on sure_fg
n_markers, markers = cv2.connectedComponents(sure_fg)
print(f"Connected components found: {n_markers - 1} (excluding background)")

# Convention: background must be 1 (not 0), unknown must be 0
markers = markers + 1
markers[unknown == 255] = 0

# Step 6: watershed — modifies markers in place; boundaries become -1
markers = cv2.watershed(coins, markers)

# Visualize
result = coins.copy()
result[markers == -1] = [0, 0, 255]   # boundaries in red
print(f"After watershed: {markers.max()} regions, "
      f"boundary pixels: {(markers == -1).sum():,}")

# Pretty marker map (random colors per label)
rng = np.random.default_rng(42)
colors = rng.integers(50, 255, size=(markers.max() + 2, 3), dtype=np.uint8)
colors[0] = [0, 0, 0]  # boundary -1 stays "off"
marker_vis = colors[markers + 1]  # shift -1 to 0

show_grid([coins, dist / dist.max(), marker_vis, result],
          ["Original", "Distance transform", "Watershed labels", "Boundaries (red)"],
          cols=2, figsize=(11, 9))

