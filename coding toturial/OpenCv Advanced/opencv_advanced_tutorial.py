"""---
# 9. 🤝 Feature Matching, RANSAC, and Homography

The full pipeline for **finding a known object in a scene** (or aligning two views):

1. Detect features in both images
2. Compute descriptors
3. Match descriptors (brute-force or FLANN)
4. Filter matches with **Lowe's ratio test**
5. Estimate a **homography** (3×3 perspective transform) using **RANSAC** to reject outliers
6. Use the homography to draw the object's outline / warp one image onto the other

**Robotics relevance**: planar pose estimation, image registration, mosaicking, loop closure in SLAM.

"""

# We'll detect a cropped region of the astronaut image inside the full image,
# after rotating + scaling the crop. This simulates "find this object in the scene".
full = cv2.imread('images/astronaut.jpg', cv2.IMREAD_GRAYSCALE)
H, W = full.shape

# Build a "query" by cropping + rotating + scaling
crop = full[60:340, 100:380].copy()
M = cv2.getRotationMatrix2D((crop.shape[1]/2, crop.shape[0]/2), angle=25, scale=0.8)
query = cv2.warpAffine(crop, M, (crop.shape[1], crop.shape[0]))

# 1. Detect SIFT in both
sift = cv2.SIFT_create()
kp1, des1 = sift.detectAndCompute(query, None)
kp2, des2 = sift.detectAndCompute(full,  None)
print(f"Query keypoints: {len(kp1)}, Scene keypoints: {len(kp2)}")

# 2. Brute-force matcher with k=2 nearest neighbors per descriptor
bf = cv2.BFMatcher(cv2.NORM_L2)
knn = bf.knnMatch(des1, des2, k=2)

# 3. Lowe's ratio test — a match is "good" if the best is much better than the 2nd best
good = [m for m, n in knn if m.distance < 0.75 * n.distance]
print(f"Raw matches: {len(knn)}, after ratio test: {len(good)}")

# 4. Need at least 4 matches for homography
MIN_MATCH_COUNT = 10
if len(good) >= MIN_MATCH_COUNT:
    src_pts = np.float32([kp1[m.queryIdx].pt for m in good]).reshape(-1, 1, 2)
    dst_pts = np.float32([kp2[m.trainIdx].pt for m in good]).reshape(-1, 1, 2)

    # RANSAC: tries random 4-point subsets; keeps the homography with the most inliers
    Hmat, mask = cv2.findHomography(src_pts, dst_pts, cv2.RANSAC, 5.0)
    matches_mask = mask.ravel().tolist()
    inliers = sum(matches_mask)
    print(f"RANSAC inliers: {inliers} / {len(good)} ({100*inliers/len(good):.1f}%)")
    print(f"Homography matrix:\n{Hmat}")

    # 5. Project the query's corners through the homography to find its location in the scene
    h, w = query.shape
    corners = np.float32([[0, 0], [0, h - 1], [w - 1, h - 1], [w - 1, 0]]).reshape(-1, 1, 2)
    projected = cv2.perspectiveTransform(corners, Hmat)

    scene_vis = cv2.cvtColor(full, cv2.COLOR_GRAY2BGR)
    cv2.polylines(scene_vis, [np.int32(projected)], True, (0, 255, 0), 3, cv2.LINE_AA)

    # 6. Draw the inlier matches
    draw_params = dict(matchColor=(0, 255, 0), singlePointColor=None,
                        matchesMask=matches_mask, flags=2)
    matches_vis = cv2.drawMatches(query, kp1, scene_vis, kp2, good, None, **draw_params)
    show(matches_vis, title=f"SIFT + RANSAC: {inliers} inliers, query located",
         figsize=(15, 5))
else:
    print(f"Not enough matches: {len(good)}")

