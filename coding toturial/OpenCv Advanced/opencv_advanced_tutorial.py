def to_bgr(arr):
    """Convert a skimage array (RGB or gray, uint8) to OpenCV-compatible BGR/gray uint8."""
    if arr.dtype != np.uint8:
        arr = (255 * (arr - arr.min()) / max(1e-9, arr.max() - arr.min())).astype(np.uint8)
    if arr.ndim == 3:
        return cv2.cvtColor(arr, cv2.COLOR_RGB2BGR)
    return arr

