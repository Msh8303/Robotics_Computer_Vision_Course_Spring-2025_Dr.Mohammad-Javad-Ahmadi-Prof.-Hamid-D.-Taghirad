"""---
# 19. 🌅 HDR Imaging — Combining Multiple Exposures

A single photo can capture only ~6 stops of dynamic range. Real scenes (sunlit clouds + shadowed interior) span 14+ stops. **HDR** captures multiple exposures and fuses them.

OpenCV provides:
- `cv2.createMergeMertens()` — *exposure fusion*, no exposure-time information needed (simplest)
- `cv2.createMergeDebevec()` — true HDR radiance map (needs exposure times)
- `cv2.createTonemapReinhard()` — compress HDR back to displayable range

We simulate three exposures by gamma-shifting one image.

"""

orig = cv2.imread('images/coffee.jpg')

# Simulate underexposed / normal / overexposed
def gamma(img, g):
    inv = 1.0 / g
    table = (np.arange(256) / 255.0) ** inv * 255.0
    return cv2.LUT(img, table.astype(np.uint8))

under  = gamma(orig, 0.4)
normal = orig
over   = gamma(orig, 2.5)
print("Mean pixel intensity per exposure:")
print(f"  under  : {under.mean():.1f}")
print(f"  normal : {normal.mean():.1f}")
print(f"  over   : {over.mean():.1f}")

# === Mertens exposure fusion (simplest, no exposure times) ===
merge_mertens = cv2.createMergeMertens()
fusion = merge_mertens.process([under, normal, over])
fusion = np.clip(fusion * 255, 0, 255).astype(np.uint8)

show_grid([under, normal, over, fusion],
          ["Underexposed", "Normal", "Overexposed", "Mertens fusion"],
          cols=2, figsize=(12, 9))

